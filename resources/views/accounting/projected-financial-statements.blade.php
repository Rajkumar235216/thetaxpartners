	<div class="post-entry">
		<div class="post-entry-text">
		<p><span style="font-size: 14pt;"></span></p>
<p>&nbsp;</p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projected financial statements take into account past financial trends, market conditions, possible changes and management expectations to arrive at a future financial picture. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Accounting alone only looks at past financial data. That is, the expenses you’ve already incurred and income you’ve already earned. As well as the assets and liabilities you currently have on the books.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">While this information is critical in assessing your company’s results and decision making, it does not give you enough insight into your future performance. &nbsp;</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Think of accounting and bookkeeping as step one. Your business has dozens if not, hundreds or thousands of transactions monthly. In order for you to make any sense of it all, you need bookkeeping to organize this information into financial statements. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">And those financial statements are reviewed regularly to help you make profitable decisions. But how can you measure the impact of those decisions? How can you see the financial picture those decisions will make?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projected financial statements.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Consider your projected financial statements as your step two in the accounting and bookkeeping process. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">You want your business to have longevity. Apart of that is planning and preparing for the future. Projected financial statements help you do just that. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><strong>Breaking Down Projected Financial Statements Further</strong></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh6.googleusercontent.com/fHvA05wH0uFv4xRyjcLic4iijU-s11DC1e7qfhGj1VrRbD3bKNFzbaFyXzf9tkDXwtcImtSbHM-PtK8p9Ob_uT4GsMIw6zHr3lrFWNUOnlF0USbw286GUtuP9vRk56vMMj85L-FC" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projected financial statements are also called pro forma financial statements. The term pro forma simply means “as a matter of form”. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">In the business world, pro forma, or projected financial statements, are typically used to focus on certain figures, such as sales or profit. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projected financial statements are most effectively used to examine the effects of a particular decision.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">For example, if you are considering adding a new product line, you would use pro forma financial statements to see how the addition would impact your business overall. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projected financial statements look similar, if not, the same as regular financial statements. So when examining them, you’ll see the same accounts and structure as you would on your normal statements. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Pro forma financial statements can consist of a balance sheet, profit or loss, cash flow statement or more. Most statements that are produced to reflect present financial information can be translated to present future information. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">You can see what your business is projected to look like at some point in the future. Planning never seemed so easy!</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><strong>Why Projected Financial Statements Should be a Regular Part of Your Accounting?</strong></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh3.googleusercontent.com/0Vxi0Q0TaNfks3dw8eYoWdFAWo0gm72QCOZT76-7s3s0dHV70zDyCL0QK_U2tNZFISnnat2LFUy5g_zaJ0BtXmz5CJoauSQ-JkBtKcmzlbBiJsNnD1c1lx5nezgJe2mYZyT_98cJ" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">You guessed it. Planning. Projected financial statements help you plan for the future. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Given the many different decisions and scenarios you’re faced with every day, projections help you decipher between them all. This allows the decisions you do make to be backed by numbers and financial analysis.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Let’s think of it this way. When you make a major decision in your personal life, how do you go about choosing the best option?</span></p><div class="lyfea-paragraph" id="lyfea-1932187734"><span class="cp-trigger-shortcode cp-trigger-cp_id_e0790 cp-cp_id_e0790"></span><link rel="stylesheet" id="every_design-cp_id_e0790" href="https://lyfeaccounting.com/blog/wp-content/plugins/convertplug/modules/modal//assets/demos/every_design/every_design.min.css" type="text/css" media="all"><style type="text/css"></style><link rel="stylesheet" type="text/css" id="cp-google-fonts" href="https://fonts.googleapis.com/css?family=ABeeZee|Abel|Abril+Fatface|Aguafina+Script|Aladin|Alegreya+SC|Alegreya+Sans|Alex+Brush|Alfa+Slab+One|Alice|Amarante|Amatic+SC|Amita|Architects+Daughter|Arizonia|Arvo|Average+Sans|Bad+Script|Berkshire+Swash|Bevan|Bigshot+One|Bitter|Black+Ops+One|Bowlby+One|Cabin|Cabin+Condensed|Cabin+Sketch|Cherry+Cream+Soda|Codystar|Coming+Soon|Cookie|Cutive+Mono|Duru+Sans|Emilys+Candy|Fjalla+One|Freckle+Face|Fredericka+the+Great|Gabriela|Grand+Hotel|Great+Vibes|Habibi|Happy+Monkey|Julius+Sans+One|Just+Another+Hand|Kaushan+Script|Lato|Libre+Baskerville|Lobster|Lobster+Two|Londrina+Sketch|Maven+Pro|Montserrat|Neuton|Open+Sans|Oregano|Oswald|Pacifico|Plaster|Playball|Pontano+Sans|Quicksand|Raleway|Roboto|Roboto+Condensed|Roboto+Mono|Roboto+Slab|Sacramento|Source+Sans+Pro|Tenor+Sans|Varela+Round|Lato|Open Sans|Libre Baskerville|Montserrat|Neuton|Raleway|Roboto|Sacramento|Varela Round|Pacifico|Bitter" media="all" onload="if(media!=&quot;all&quot;)media=&quot;all&quot;">		
