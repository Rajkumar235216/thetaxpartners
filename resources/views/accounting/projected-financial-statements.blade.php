<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">First, you examine the possible options and their corresponding outcomes. From there, you choose the option with the most favorable results. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">You need to have this sort of analysis as part of your business decision making as well. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projected financial statements is the way to review your possible outcomes before making a decision. While no projection is perfect, having one mitigates the risk of uncertainty and losses.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><strong>Projections Vs Forecasts: What’s the Difference?</strong></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh5.googleusercontent.com/3HSHWhrScFBuL9aJ4Yuu9Sqbvf0_S-3XkyRBBlvfPBOVrLIEywPrnQQBhUiK2FcvfYT199-M7U2t1cDS8KX1hxf1L3MUz86QfvBymU7g7TSLs3asZHH4PPVeO_Q73PuggcZTACK6" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Projections and forecasts are not one and the same. Though, people tend to use these terms interchangeably. Maybe even yourself!</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">The difference between the two is slight, yet significant. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Remember when we said projected financial statements are used to help assess a possible business decision? A financial forecast operates differently. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Instead, a financial forecast only takes into account <em>current</em> conditions and expectations to arrive at a future value. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Clear as mud? Let us explain.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Financial forecasts are predictions of future financial performance based on today’s facts. If nothing were to change in your business, what would your numbers look like? That’s a financial forecast.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">On the other hand, a financial projection considers a <em>hypothetical</em> course of action. In our previous example, we considered adding a new product line and how that would impact your financial results. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">At larger companies, financial forecasts are what’s provided to external users. For example, if you are seeking outside funding from investors or banks, financial foreasts are used to assess your company’s sustainability.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">In general, you should expect the objectives in a forecast to be met.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Again, projections have a speculative element. An element that is not true today but if it was, what would that look like?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Financial projections are more geared towards internal users to aid in decision making. However, certain external users, like investors, may want to see the financial impact of a particular decision. A financial projection would show this. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">You could also think of projected financial statements as a way to answer ‘what if’ questions. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">“What if I added a new service?”</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">“What if I invested in new equipment?”</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">“What if I took out a bank loan?”</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">“What if I added a new employee?”</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">The list goes on. </span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Both financial forecasts and projections have important roles in your business. It’s important to understand the difference between the two and how to use them. Both can be used to drive profitability and growth within your business.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><strong>How to Prepare Projected Financial Statements?</strong></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh3.googleusercontent.com/XRQSW76nE73miiTofDDmEb_muQVElMdYwy3pkNTJbzMh6wjhEcfC1g-4dp_CwXapJ7ZPP8sTYt3DYzSNktpctxAy-BTkAs8__JSLnqMXyPSbOwL6Ig6tDBUBK9Bh4ybQ8-KaZlIx" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Preparing regular financial statements can be hard. So there’s no doubt that projected financial statements have its difficulties.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">This is where the help of a CPA or CFO comes to play.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Full-time CFOs can be pricey for a small business. But consider hiring a part-time CFO. They have the expertise a full time CFO would have at a small fraction of the price.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">In either case, there are some general steps a CFO does to prepare projected financial statements.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><em>1. Examine comparative reports.</em> Assuming you have accounting and bookkeeping records, the first step is comparing results across periods.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Compare this year’s profit and loss to last year’s, and the year before. What trends do you notice?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Has profit increased? Why?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Did you implement any major changes during this time? And how has it impacted your financials? By what percentage?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">This exercise should be done for each of your main financial statements. (I.e. profit and loss, balance sheet, cash flow statement, etc.).</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">2.<em>Safely make assumptions.</em> The primary purpose of projecting your financial statements is to present ‘what if’ scenarios and make decisions based on those projections.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh4.googleusercontent.com/fkXwG8tcZZdnOPrKHwWWWSGYEEhRuw6iEYPAkhi4RLpff3orkWjPhQiwEray3LeSlC9Q5PG1CRowc-6hCTLQGdqhh24xuInEeAoa04ETCQF4TwNFYRbb_8rsf9gCBnZ69-cTazYY" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Not every account on your financial statement is impacted by a ‘what if’ scenario. Therefore, you should safely assume those accounts will remain the same or deviate at the same rate historically.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">For example, if you want projected financial statements prepared to show the effects of a new service line, your rent expense is not likely impacted by this.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Therefore, if you pay $500 per month for rent, you should expect that amount to be the same on your projected financial statement.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">The same is true for other accounts. Not every account would be dependent on your ‘what if’. These accounts are considered independent variables.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Adding to this example, maybe your rent expense consistently goes up 3% per year. This should be reflected in your projected financial statements.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Even though rent expense is independent of adding a new service line, history shows that it increases 3% per year.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><em>3. Make projections on relevant accounts. </em>Once independent accounts have been projected from step 2, it is time to make reasonable projections on the other accounts.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Again, using our adding a service line example. Consider which accounts would be impacted.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">How are sales affected? Expenses? Assets?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Would there be any new accounts added? Are there any that would be eliminated?</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Answering questions like these will help in the preparation of your projections.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Coming up with the actual number of sales or expenses the new service line will earn or incur is the trickiest part.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Your business trends, ratio analysis, market conditions, and risk analysis should all be employed to arrive these numbers.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Once your projections are complete, you have more leverage in making the best possible decisions for your business.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><strong>Your CFO’s Role</strong></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh6.googleusercontent.com/24KcklAHXq-9S9x5L1Vt276nDekglrLw7t_-V4wyUsObUYhgbar1zlWdVaiVIExjvRQFRpArxN7EIisZ7VXHTn05XA_5QYhw5g2XnNyqCyE3pvHnBYpBs2KK6UlJcACIFuzExN0b" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Ratio analysis, risk analysis, and financial trends are just a few concepts that can sound foreign to a non-financial expert. Yet, these are the necessary elements for preparing a financial projection.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">A CFO has the expertise, resources, and education to apply these concepts without you having to delve too deep in the details.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">A CFO does more than preparing your financial projections. They are responsible for the monitoring and management of your company’s financial performance.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Their job is to learn and understand the details of your financial situation and provide strategy to grow your business.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Their specific duties vary from company to company. Based on your needs and goals, they will deliver the appropriate level of service to your business.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><strong>Limitations on Projected Financial Statements </strong></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh4.googleusercontent.com/PRNZMXA__2nlBUBMtwrvd0hmuZ2V66vNjzBix-fidRcvcdQ3Ku3VfrXWTzExl4MJVJXDDWV_dOG77wSSeZ-GKKb4y2lvdMzAkmRL5Z7AxwL6qJYYgjoim8Wslq9EreWA2Ctkr44t" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">As with anything that concerns the future, uncertainty exists. Uncertainty is the core limitation placed on projected financial statements. There are other limitations that you should keep in mind.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><em>Unforeseen Situations</em></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Though a thorough analysis should be performed before actually projecting out data, circumstances do exist that could alter expectations.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">The analysis and calculations could be reasonable but some unforseen situation occurred such as a recession or new competitor.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">This is why it’s important that your projections are conservative and adaptable to changes.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><em>Narrowed Focus</em></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Because financial projections focus on a ‘what-if’ scenario, a business owner might lose sight of other areas of the business.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">In the case of adding a new service line, an owner might neglect their other service lines. This can cause those areas to experience negative side effects.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Make sure to continue to monitor all areas of your business, even when implementing a decision based on projections. It’s counterproductive to grow in one area of your business but decline in every other area.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;"><em>Takes Time</em></span></p>
<p><span style="font-size: 14pt;">

</span></p>
<figure class="wp-block-image"><span style="font-size: 14pt;"><img src="https://lh5.googleusercontent.com/RHSPjvax-MMZCIpAhtVX5cEqIoxI0Hfq_gNzsJHzai6KkEuWp2OWSZ2H-dxWo9krjteh2yVX3QmOHH0QOQ0E9rg7-mTaCX_WpJujGB2Q0uWqsAPodnt-HNOXy2WyEm2AAUmdJbh6" alt="Projected Financial Statements"></span></figure>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Developing projected financial statements takes time. Time to gather financial data, market conditions, competitor information and more.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Time that you may not have. Especially, if you’re a non-expert in this area.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">What ends up happening is your projections end up being wrong, incomplete or just not done. But if you want to see growth and expansion in your business, preparing financial projections should not be ignored.</span></p>
<p><span style="font-size: 14pt;">

</span></p>
<p><span style="font-size: 14pt;">Instead, consider hiring a part-time CFO. They have the time, knowledge and know how to accomplish this for you.</span></p>
