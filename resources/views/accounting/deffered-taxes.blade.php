<p><span style="font-weight: 400;"><br>
With respect to timing differences related to unabsorbed depreciation or carry forward losses, DTA is recognised only if there is future virtual certainty. It means DTA can be realized only when the company reliably estimates sufficient future taxable income. This test for virtual certainty has to be done every year on balance sheet date and if the condition is not fulfilled, such DTA/DTL should be written off.</span></p>
<p><span style="font-weight: 400;">While computing future taxable income, only profits pertaining Business and Professional should be considered and not the income from other sources.</span></p>
<h2 id="Virtual"><strong>Example for Virtual Certainty</strong></h2>
<p><span style="font-weight: 400;">Future profits projection prepared by an entity based on the future restructuring, sales estimation, future capital expenditure past experience etc which are submitted to banks for loan is a concrete evidence for virtual certainty. But virtual certainty cannot be convincing if it’s only based on some say binding export order which has the risk of cancellation anytime.</span></p>
<h2 id="Deferred"><strong>Example of Deferred Tax Asset and Liability</strong></h2>
<p><b>DTA</b><span style="font-weight: 400;"> – Say Book profit of an entity before taxes is Rs. 1000 and this includes provision for bad debts of Rs.200. For the purpose of tax profit, bad debts will be allowed in future when it’s actually written off. Hence taxable income after this disallowance will be Rs. 1200 and let’s say income tax rate is 20% then the entity will pay taxes on Rs. 1200 i.e (1200*20%) Rs. 240.</span></p>
<p><span style="font-weight: 400;">If bad debts were not disallowed, entity would have paid tax on Rs. 1000 i.e (1000*20%) Rs 200. For the additional Rs. 40 which is already paid now, we have to create DTA. Entry for recording DTA is as under:</span></p>
<ul>
<li><span style="font-weight: 400;">Deferred Tax Asset Dr&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 40</span></li>
<li><span style="font-weight: 400;">To Deferred Tax Expense Cr &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;40</span></li>
</ul>
<p><span style="font-weight: 400;">(Being DTA of Rs. 40 accounted in the books)</span></p>
<p><b>DTL</b><span style="font-weight: 400;"> – Common example of DTL would be depreciation. When the <a href="https://cleartax.in/s/depreciation-income-tax-act">depreciation rate per Income tax</a> act is higher than the depreciation rate per companies act (generally in the initial years), entity will end up paying less tax for the current period. This will create deferred tax liability in the books:</span><img class="alignnone wp-image-17446 size-full img-responsive" src="https://assets1.cleartax-cdn.com/s/img/2018/02/19190634/516.jpg" alt="DTL/DTA" srcset="https://assets1.cleartax-cdn.com/s/img/2018/02/19190634/516.jpg 1208w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190634/516-300x169.jpg 300w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190634/516-768x432.jpg 768w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190634/516-1024x576.jpg 1024w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190634/516-1200x675.jpg 1200w" sizes="(max-width: 709px) 85vw, (max-width: 909px) 67vw, (max-width: 1362px) 62vw, 840px" width="1208" height="680"></p>
<p><span style="font-weight: 400;">There is no DTA or DTL provisions made for permanent differences. Eg. Fines and Penalties which are part of book profits but never allowed for tax purpose. Hence this difference created will be a permanent difference.</span></p>
<p><span style="font-weight: 400;">DTA is presented under Non-Current Asset and DTL under the head Non-Current Liability. Both DTA and DTL can be adjusted with each other provided if they are legally enforceable by law and there is an intention to settle the asset and liability on a net basis.</span></p>
