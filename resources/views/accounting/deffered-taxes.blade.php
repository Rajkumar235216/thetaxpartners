<h2 id="Timing"><strong>Timing Difference</strong></h2>
<p><span style="font-weight: 400;">Company derives its book profits from the financial statements prepared in accordance with the rules of companies act and it calculates its taxable profit based on provision of the Income-tax Act. </span></p>
<p><span style="font-weight: 400;">There is a difference between the book profit and taxable profit because of certain items which are specifically allowed and disallowed for tax purpose. This difference between the book and the taxable income or expense is known as timing difference and it can be either:</span></p>
<p style="padding-left: 30px;"><span style="font-weight: 400;"><strong>1. Temporary Difference</strong> – Differences between book income and tax income which are capable of reversing in subsequent period</span></p>
<p style="padding-left: 30px;"><span style="font-weight: 400;"><strong>2. Permanent Difference</strong> – Differences between book income and tax income which are not capable of reversing in subsequent period</span></p>
<p><img class="alignleft wp-image-17445 size-full img-responsive" src="https://assets1.cleartax-cdn.com/s/img/2018/02/19190616/329.jpg" alt="Timing Difference" srcset="https://assets1.cleartax-cdn.com/s/img/2018/02/19190616/329.jpg 1147w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190616/329-300x169.jpg 300w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190616/329-768x432.jpg 768w, https://assets1.cleartax-cdn.com/s/img/2018/02/19190616/329-1024x576.jpg 1024w" sizes="(max-width: 709px) 85vw, (max-width: 909px) 67vw, (max-width: 1362px) 62vw, 840px" width="1147" height="645"></p>
<p>&nbsp;</p>
<h2 id="DT"><strong>Deferred Tax (DT)</strong></h2>
<p><span style="font-weight: 400;">The tax effect on the timing differences is termed as deferred tax which literally means taxes which are deferred. Deferred tax is recognised on all timing difference – Temporary and Permanent. </span></p>
<p><span style="font-weight: 400;">These deferred taxes are given effect to in the financial statements through Deferred Tax Asset and Liability as under:</span></p>
<div class="table-responsive"><table class="table table-bordered">
<tbody>
<tr>
<td style="text-align: center;"><b>Sl.No</b></td>
<td style="text-align: center;"><b>Entity Profit Status</b></td>
<td style="text-align: center;"><b>Entity – Current</b></td>
<td style="text-align: center;"><b>Entity – Future</b></td>
<td style="text-align: center;"><b>Effect</b></td>
</tr>
<tr>
