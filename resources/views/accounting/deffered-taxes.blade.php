<h2 id="Effect"><strong>Effect of Tax Holiday w.r.t DTA/ DTL</strong></h2>
<p><span style="font-weight: 400;">Tax Holiday is a benefit provided to new undertakings established in free trade zone, 100% export oriented undertakings etc under section 10A, 10B of the Income Tax Act, 1961. To encourage production, consumption of certain items, the government removes certain taxes for a temporary period subject to certain condition. DT from the timing difference that reverses during the tax holiday period should not be recognised during the enterprise’s tax holiday period. DT related to the timing difference that reverses after the tax holiday has to be recognised in the year of origination.</span></p>
<div style="padding: 40px; margin: 20px 0; border: 1px solid #ccc; background: #fafafa; text-align: justified;">
