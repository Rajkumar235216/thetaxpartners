<h2 id="MAT"><strong>Effect on MAT w.r.t DTA/ DTL</strong></h2>
<p><span style="font-weight: 400;">MAT is Minimum Alternate Tax which a company is required to pay if its tax payable as per normal provision of the income tax act is less than the tax computed @ 18.5% of the book profit. MAT is levied under section 115JB of the income tax act and it is calculated using the entity’s book profit as under:</span></p>
<p><span style="font-weight: 400;">Book profit is increased by the following:</span></p>
<ol>
<li style="font-weight: 400;"><span style="font-weight: 400;">Income tax paid or provision </span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">An amount carried to any reserve</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Provisions made for unascertained liabilities</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Deferred tax provision etc</span></li>
</ol>
<p><span style="font-weight: 400;">And it is decreased by the following:</span></p>
<ol>
<li style="font-weight: 400;"><span style="font-weight: 400;">Amount withdrawn from any reserve or provision</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Depreciation debited to P&amp;L (except revaluation depreciation)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Lesser of Loss brought forward or unabsorbed depreciation &nbsp;</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Deferred tax credited to P&amp;L etc.</span></li>
</ol>
<p><span style="font-weight: 400;">There are controversies if deferred tax liability debited to P&amp;L should be added to the book income for the purpose of MAT calculation. Kolkata Tribunal in Balrampur Chini’s case has held that the deferred tax liability should not be added back whereas the Chennai Tribunal in Prime Textiles Ltd’s case has held otherwise.</span></p>
<p>&nbsp;</p>
<div style="padding: 40px; margin: 20px 0; border: 1px solid #ccc; background: #fafafa; text-align: justified;">
<p><i><span style="font-weight: 400;">“Deferred tax charge is not a provision for tax but is a provision for tax effect for difference between taxable income and accounting income and further that deferred tax charge cannot be termed as income-tax paid or payable, which has to be paid out of the profit earned. Reserves mentioned in Section 115JB is different, it can be unilaterally transferred back to P&amp;L account or can be utilised for issuing bonus shares etc. However, amounts created towards deferred tax charge cannot be so transferred or utilized”</span></i></p>
</div>
<div style="padding: 40px; margin: 20px 0; border: 1px solid #ccc; background: #fafafa; text-align: justified;"><i><span style="font-weight: 400;">“The Chennai Tribunal observed that AS-22 is mandatory as per Section 211(3) of the Companies Act, however, the same is not notified by the Central Government under Section 145(2) of the IT Act. Moreover, the deferred tax liability cannot be considered as ascertained liability and therefore, assessing officer has every power to make adjustment on this account as it cannot be termed as tinkering of audited accounts prepared in accordance with the provisions of the Companies Act.”</span></i></div>
<p><span style="font-weight: 400;">As seen, there are conflicting judgments on this and this requires clarification from the government or decision by the high court.</span></p>
<h2 id="credit"><strong>Whether MAT credit can be considered as a deferred tax asset per AS 22?</strong></h2>
<p><span style="font-weight: 400;">As per AS 22 deferred tax asset and liability arise due to the difference between book income &amp; taxable income and do not rise on account of tax expense itself. MAT does not give rise to any difference between book income and taxable income. It is not appropriate to consider MAT credit as a deferred tax asset in accordance with AS 22. </span></p>




