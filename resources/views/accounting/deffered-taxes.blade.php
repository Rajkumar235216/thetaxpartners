        <h1 class="title">Deferred Tax Asset and Deferred Tax Liability</h1>
				<p class="subtitle"></p>
				<p class="timestamp">Updated on <time datetime="2019-11-27T12:51:57">Nov 27, 2019 - 12:51:57 PM</time></p>
			</div>
			<div class="content">
				<p>Deferred Tax Liability (DTL) or Deferred Tax Asset (DTA) item forms an important part of your Financial Statements. This adjustment made at year-end closing of Books of Accounts affects the Income-tax outgo of your Business&nbsp;for that year&nbsp;as well as the years ahead.</p>
<div style="padding: 40px; margin: 20px 0; border: 1px solid #ccc; background: #fafafa; text-align: justified;">
<p style="text-align: justify;">We got you a write on all about DTL/ DTA, How it’s calculated and certain specific Implications. In this article we cover the following topics:</p>
