        <h4>What is meant by cancellation of gst registration?</h4>
        <p>Cancellation of GST registration simply means that the taxpayer will not be a
        GST registered person any more. He will not have to pay or collect GST.</p>
        <h4>Consequences of Cancellation </h4>
        <p>
        <ul>
            <li>The taxpayer will not pay GST anymore/li>
            <li>For certain businesses, registration under GST is mandatory. If the GST registration is cancelled and
                business is still continued, it will mean an offence under GST and
                heavy penalties will apply.
                </li>
        </ul>
        </p>
        <h4 >Who can cancel the GST registration?</h4>

        <h5>Cancellation when Turnover is less than 20 lakhs</h4>
        <p>Every person who was registered under old laws had to mandatorily migrate to
                GST. Many such persons are not liable to be registered under GST. </p>
        <p>For example, the threshold under VAT in most states was 5 lakhs whereas it is
                20 lakhs under GST. However, do make sure you are not making inter-state supplies since registration is
                mandatory for inter-state suppliers except for service providers. </p>
        <p>Such a taxpayer can submit an application electronically in FORM GST REG-29
                at the common portal.</p>
        <p>The proper officer shall, after conducting an enquiry as required will cancel
                the registration.</p>

        <h5>Cancellation by taxpayer in other cases</h5>
        
        <ol>
            <li>The business has been discontinued.</li>
            <li>The business has been transferred fully, amalgamated, demerged or
                    otherwise disposed —The transferee (or the new company from amalgamation/ demerger) has to get
                    registered. The transferor will cancel its registration if it ceases to exist.</li>
            <li>There is a change in the constitution of the business &nbsp; (For
                    example- Private limited company has changed to a public limited company)</li>
        </ol>
        <h4>Forms for cancellation</h4>
        <p>All those who cannot follow the above method must file an application for
                cancellation in FORM GST REG 16. The legal heirs of the deceased taxpayer will follow the same procedure
                as below.</p>
        <ul>
            <li >Application&nbsp;for cancellation has to be
                    made in FORM GST REG 16.</li>
            <li >The following details must be included in FORM GST REG 16-
                <ul>
                    <li>Details of inputs, semi-finished,
                            finished goods held in stock on the date on which cancellation of registration is
                            applied</li>
                    <li >Liability, thereon</li>
                    <li>Details of the payment.</li>
                </ul>
            </li>
            <li>The proper officer has to issue an order for cancellation in FORM GST
                    REG-19 within 30 days from date of application. The cancellation will be effective from a date
                    determined by the officer and he will notify the taxable person.</li>
        </ul>
        <h4 >Cancellation by tax officer </h4>
        <h5>Why will the officer cancel registration?</h5>
        <p>The registration can be cancelled, if the taxpayer-</p>
        <p>>(a) Does not conduct any business from the declared place of business
                OR</p>
        <p>(b) Issues invoice or bill without supply of goods/services (i.e., in
                violation of the provisions) OR</p>
        <p>(c) Violates the anti-profiteering provisions (for example, not passing on
                benefit of ITC to customers)</span></p>
        <h5>Procedure</h5>
        <p>
        <ul>
            If the proper officer has reasons to cancel the registration of a person then
            <li>The person will send a show cause notice to such person in FORM GST REG-17.</li>
        </ul>
        <ul>
            <li>The person must reply in FORM REG–18 within 7
                    days from date of service of notice why his registration should not be cancelled.</li>
        </ul>
        <ul>
            <li>If the reply is found to be satisfactory, the
                    proper officer will drop the proceedings and pass an order in FORM GST REG –20.</li>
        </ul>
        <ul>
            <li>If the registration is liable to be cancelled,
                    the proper officer will issue an order in FORM GST REG-19. The order will be sent within 30 days
                    from the date of reply to the show cause.</li>
        </ul>
        </p>
        
