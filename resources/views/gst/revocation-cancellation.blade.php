     <h4>What is revocation of cancellation of registration?</h4>
        <p>Revocation means the official cancellation of a decision or promise.
            Revocation of cancellation of registration means that the decision to
                cancel the registration has been reversed and the registration is still valid.</p>
        <h4>When is revocation of cancellation applicable?</h4>
        <p>This is applicable only when the tax officer has cancelled the
                registration of a taxable person on his own motion. Such taxable person can apply to the officer
                for revocation of cancellation within thirty days from the date of
                the cancellation order.</p>
        <h4>Procedure</h4>
        <p>
        <ul>
            <li>A registered person can submit an application
                    for revocation of cancellation, in FORM GST REG-21, if his registration has been
                    cancelled by the proper officer.</li>
        </ul>
        <ul>
            <li >He must submit it within 30 days from the date
                    of service of the cancellation order at the Common Portal.</li>
        </ul>
        <ul>
            <li >If the proper officer is satisfied he can
                    revoke the cancellation of registration by an order in FORM GST REG-22 within 30 days from the date
                    of receipt of the application. Reasons for revocation of cancellation of registration must be
                    recorded in writing.</li>
        </ul>
        <ul>
            <li >The proper officer can reject the application
                    for revocation by an order in FORM GST REG-05 and communicate the same to the applicant.</li>
        </ul>
        <ul>
            <li>Before rejecting, the proper officer
                    must&nbsp;issue a show cause notice in FORM GST REGâ€“23 for the applicant to show why the application
                    should not be rejected. The applicant must reply in FORM GST REG-24 within 7 working days from the
                    date of the service of notice.</li>
        </ul>
        <ul>
            <li>The proper officer will take decision within
                    30 days from the date of receipt of clarification from the applicant in FORM GST REG-24.</li>
        </ul>
        </p>

