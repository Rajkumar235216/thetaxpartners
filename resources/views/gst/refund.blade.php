    <p>One should be conversant about who can claim refund in GST. The following are the most common refund rules under GST:</p>
<ul>
	<li><strong>Tax paid on inward supply of goods and/or services which have been exported or on inputs or input services used in goods and/or services exported.&nbsp;</strong>Please note, that if the goods are subjected to export duty, refund will not be allowed.</li>
	<li><strong>Unutilized input tax credit due to output supplies being exports or zero rated supplies</strong></li>
	<li><strong>Unutilized input tax credit due to inverted duty structure.</strong>&nbsp;This is when the rate of tax on inputs is higher than the rate of tax on output supplies. Please note, that in this case, refund is not applicable when supplies are NIL rated or fully exempt.</li>
</ul>

<h4>GST Refund Process</h4>
<h5 >Application for Refund</h5>
<p>A person claiming refund of tax or interest or any other amount paid must file an application for refund in Form GST RFD-1 before the expiry of 2 years from the ‘relevant date’. The ‘relevant date’ in each scenario of refund is given below:</p>
<ul >
	<li><strong>Goods exported by sea or air –&nbsp;</strong>Date on which the ship or aircraft in which the goods are loaded, leaves India</li>
	<li><strong>Goods exported by land –&nbsp;</strong>Date on which the goods pass the frontier</li>
	<li><strong>Goods exported by post –&nbsp;</strong>Date of dispatch of goods by the concerned post office</li>
	<li><strong>Services exported, where the supply of service has been completed prior to the receipt of payment –&nbsp;</strong>Date of receipt of payment</li>
	<li><strong>Services exported, where the payment has been received in advance, prior to the date of issue of invoice –&nbsp;</strong>Date of issue of invoice</li>
	<li><strong>Unutilized input tax credit –</strong>&nbsp;End of the financial year in which the claim for tax refund arises</li>
</ul>
<p><strong>Note:&nbsp;</strong>A claim for refund of the balance in the electronic cash ledger must be made through the relevant monthly return, i.e.<strong>&nbsp;Form</strong>&nbsp;<a class="text-links" href="https://tallysolutions.com/gst/gstr3-filing-format-due-date/" target="_blank" rel="noopener noreferrer">GSTR-3&nbsp;</a>in case of a regular dealer, and&nbsp;<strong>Form GSTR-4</strong>&nbsp;in case of a composition dealer.</p>
<h5>The documents required for the refund are as follows:</h5>
<ul >
	<li><strong>If the amount claimed as tax refund is less than INR 5 Lakhs –</strong>&nbsp;The person needs to file a declaration, based on the documents or other evidence available with him, certifying that the incidence of tax or interest being claimed as refund has not been passed on to another person.</li>
	<li><strong>If the amount claimed as refund is more than INR 5 Lakhs –&nbsp;</strong>The application for refund must be accompanied by:</li>
	<li>o Documentary evidence to establish that the refund is due to the person.</li>
	<li>o Documentary or other evidence to establish that the amount was paid by him/her, and that the incidence of the tax or interest has not been passed on to another person.</li>
</ul>
<h4 >Order for Refund</h4>
<p>If the refund is on account of export of goods and/or services, the authorised officer will refund 90% of the total amount claimed as refund on a provisional basis in Form GST RFD-4. Thereafter, after due verification of the documents furnished, the officer will issue an order for final settlement of the refund claim.</p>
<p>Provisional refund will be granted subject to the following conditions:</p>
<ul >
	<li>The person claiming refund has not been prosecuted for tax evasion of an amount exceeding Rs. 250 Lakhs during the preceding 5 years.</li>
	<li>The person’s GST compliance rating is not less than 5 on a scale of 10.</li>
	<li>No pending appeal, review or revision exists on the amount of refund.</li>
</ul>
<p>If the officer is satisfied that the whole or part of the amount claimed as refund in the application is refundable, he will issue an order for the refund in Form GST RFD-5. This will be done within 60 days from the date of receipt of application. If the refund is not sanctioned within 60 days, interest on the refund amount will be paid for the period after 60 days, till the date of actual refund of tax.</p>
<p><strong>Note:</strong>No refund shall be made if the amount claimed as refund is less than Rs. 1,000.</p>

<h4 >Exceptional Scenarios of GST Refund</h4>
<p>Following are few exceptional GST refund rules, India might come across:</p>
<ul>
	<li><strong>Tax on supply of goods regarded as deemed exports.</strong>&nbsp;E.g.: Supply of goods or services to an SEZ (Special Economic Zone) or EOU (Export Oriented Unit).</li>
	<li><strong>Tax is refundable as a consequence of a judgement, decree, order or on the direction of an Appellate Authority, Appellate Tribunal or any court.</strong></li>
	<li><strong>Tax has been paid on a supply which has not been provided, either wholly or partially, and for which an invoice has not been issued.</strong></li>
	<li><strong>Tax wrongly collected and deposited with the Central or State Government.&nbsp;</strong>If a person has paid CGST and SGST on an interstate supply or IGST on an intrastate supply, the person is eligible for refund of the amount once the tax has been remitted correctly.</li>
	<li><strong>IGST paid on supply of goods to tourists travelling out of India, if the goods are taken out of India.</strong></li>
</ul>
<p>The ‘relevant date’ in these scenarios of refund is given below:</p>
<ul>
	<li><strong>Goods regarded as deemed exports –&nbsp;</strong>Date on which the return relating to the deemed exports is filed</li>
	<li><strong>Tax refundable as a consequence of a judgement, decree, order or on the direction of an Appellate Authority, Appellate Tribunal or any court -</strong>&nbsp;Date of communication of the judgement, decree, order or direction</li>
	<li><strong>Tax provisionally paid -</strong>&nbsp;Date of adjustment of tax after the final assessment</li>
	<li><strong>In the case of a person, other than the supplier -</strong>Date of receipt of goods or services by the person</li>
	<li><strong>Any other case –&nbsp;</strong>Date of payment of tax</li>
</ul>
<p>The refund process under GST remains the same for both normal as well as exceptional scenarios.</p>
