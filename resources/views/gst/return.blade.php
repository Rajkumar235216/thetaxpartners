        <h4>What are GST returns?</h4>
        <hr>
        <p>Any person who applies for GST registration will also have to make a GST return filing. The GST return is
            basically a document which is needed as per the Indian tax authorities of law, to be filed. It will be used
            by the authorities of tax for the purpose of calculating the tax liability.</p>
        <h4>Who is eligible for GST returns?</h4>
        <p>Business owners and dealers who are registered under GST must file two monthly GST returns and one annual GST
            returns. The nature of the business also dictates the GST that is to be filed. GST returns are of various
            types and late filing of GST returns will result in a penalty of Rs.100 per day till the day the GST returns
            are filed. Once filing is done, any tax liability must be promptly paid to the government.</p>
        
       <h4> Documents Required for the GST Return Filing</h4>
<p>
GST Return filing have no proper set of the documents.
Its required the mainly the sales invoice of taxable services or goods. So for the GST Return filing you have to follow some standard guidelines –
​<ul>
  <li>Prepare each invoice as per the GST Law.</li>
  <li>Maintain records online or offline as bookkeeping.</li>
  <li>Try to maintain records in your GST Filing software or else use our simplified excel sheets.</li>
</ul>

</p>
        <h4>GST Input Tax Credit Reconciliation</h4>
        <p>GST or Goods and Services Tax is an indirect tax that depends on the value added at each stage of the supply
            chain of a particular service or goods until it reaches the customer or consumer. With GST, tax is imposed
            at various stages and to nullify the cascading effect, it is designed in a way that is meant to refund all
            parties that are involved in the various stages with the exception of the final consumer. This element that
            is employed to offset the tax liability is called an input tax credit.</p>
        <h4>An insight into the GST filing procedure in India</h4>
        <p>Any business person who has taken the registration, but has not crossed the limit of exemption will have to
            comply with the step-by-step GST billing process.The Government has given threshold limit for goods and
            services which will be based on aggregate turnover 40Lakhs in case of services and 20Lakhs in case of Goods,
            provided that where such person makes taxable supplies of goods or services or both from any of the special
            category States, he shall be liable to be registered if his aggregate turnover in a financial year exceeds
            ten lakh rupees.</p>
        <p>Special category states include,</p>
        <p>State of Jammu and Kashmir and States of Arunachal Pradesh, Assam, Himachal Pradesh, Meghalaya, Sikkim and
            Uttarakhand.</p>
        <p>Once the taxpayer crosses the limit of exemption, he/she will start filing for GST returns. Even in cases
            where no taxable supplies are received or made during a particular period, the taxpayer will have to file
            the NIL return. So, there is no way that you can avoid GST filing. If you miss out filing the return of one
            period, then you will not be able to file the next period’s return.</p>
        <p>Let us simplify GST in this way- Any business will have to file monthly GST return twice and annual return
            once.This implies that as a total, you will have to file GST returns 26 times a year. The GST portal issues
            4 different types of forms for filing the GST returns. They are: </p>
        <ul>
            <li>Return for purchases</li>
            <li>Return for supplies</li>
            <li>Annual returns</li>
            <li>Monthly returns </li>
        </ul>
        <p>In case of small business taxpayers in India who have opted for a composition scheme, they just have to file
            GST returns on a quarterly basis. Return filing process can be done online.</p>

        <h4>Benefits of GST Return Filing</h4>
        <h5 class="subTop">Elimination of the cascading effect</h5>
        <p>The introduction of GST into the Indian tax system has done away with several other taxes like central excise
            duty, service tax, customs duty and state level value added tax. Thus a single GST has eliminated the
            cascading effect of tax on tax .</p>
        <h5>Higher threshold benefits</h5>
        <p>Before GST was introduced, VAT or value added tax was applicable for any business that had an annual turnover
            of 20 lakhs. Services that saw a turnover of less than 10 lakhs did not have to pay service taxes.</p>
        <h5>Startup Benefits</h5>
        <p>Earlier startups with an annual turnover of 5 lakh had to pay VAT which would be very difficult for a
            business during the initial stages. But as GST has replaced VAT, businesses can set off the service tax on
            their sales.</p>
        <h5>E-commerce for quick supply of goods</h5>
        <p>Startups are making a strong presence online offering their services and products through their websites.
            Under VAT, there were many types of VAT laws, and the supply of goods through online, that is, E-commerce
            was never a well-defined one. For instance, if you need to deliver goods to various states, then you will
            have to file the VAT declaration first. After that, you will need to provide registration details about the
            trucks which deliver the goods. In many instances, goods end up being seized by the authorities due to lack
            of proper documents. GST has now removed all such confusing processes.</p>
        <h5>Regulations and accountability</h5>
        <p>The pre-GST period witnessed a disorganized tax filing system. Presently, all taxes are paid online and major
            hassles that were a part of tax filing have been eliminated in the process of introducing GST. This has
            resulted in industries becoming more accountable and tax filing laws are better regulated than before.</p>

        <h4>Understanding GST returns in detail</h4>
        <h5 class="subTop">GSTR-1</h5>
        <p>The GSTR-1 is a monthly return form filed by businesses to report the details of the outward supplies of all
            services and goods. Every GST-registered entity should file GSTR-1 except Input service distributor and
            Composition taxpayer. It must be filed even if there is no business activity. The due date for filing GSTR-1
            is 10th of every month.</p>
        <p>The GSTR 1 acts as the base on which all other form submissions can be made for the month.</p>
        <h5>GSTR-2A</h5>
        <p>GSTR-2A is the return of details of all the inward supplies of goods/services, that is, purchases made by
            registered suppliers. This form is filed by the 15th of every month. GSTR-2A is auto populated where the
            data will be fetched from the GSTR-1 filed by the supplier. It is a read-only return and cannot be edited.
        </p>
        <h5>GSTR-2</h5>
        <p>GSTR-2 is the return filed to furnish the details of all the inward supplies of goods/services. This form can
            be edited but the filing has been suspended by the government.</p>
        <h5>GSTR-3B</h5>
        <p>It is a summarized monthly return of all the details of inward and outward supplies, input tax credits and
            the details of all the GST liabilities. It is a self-declaration form filed by all taxpayers for every tax
            period.</p>
        <p>The due date for filing GSTR-3B return form is 20th normally. However, the date may be changed if there are
            any government notifications.</p>
        <h5>GSTR-4</h5>
        <p>GSTR-4 is filed every year by all taxpayers under the GST composition scheme. Businesses should provide a
            summary of the outward supplies and all the taxes paid thereon, details of import of services and supplies
            that may attract reverse charge. </p>
        <p>The due date for filing this form is the 30th of the month succeeding the financial year.</p>
        <h5>GSTR-5</h5>
        <p>This form is filed by all non-resident taxpayers registered in India and carry out business operations in
            India. In the GSTR-5 return, businesses should furnish the details of all the inward and outward supplies
            and the tax liabilities. This form is to be submitted on a monthly basis and has to be filed by every month
            on the 20th.</p>
        <h5>GSTR-5A</h5>
        <p>Form GSTR-5A is furnished by service providers of Online Information and Database Access or Retrieval
            (OIDAR). It is filed to declare the services provided to unregistered entities or individuals, from a place
            outside India to a person in India. It is a monthly return filed by the 20th of every month.</p>
        <h5>GSTR-6A</h5>
        <p>GSTR-6A form is a system generated “draft” Statement of Inward Supplies for a Receiver Taxpayer. It is a
            read-only form.</p>
        <h5>GSTR-6</h5>
        <p>The Input Service provider generates and files GSTR-6 only after all details furnished in GSTR 6A are
            accepted and verified. This is done on the 13th of every month.</p>
        <h5>GSTR-7</h5>
        <p>GSTR-7 is filed by the entities who deduct tax at the time of making payment to the suppliers for purchase,
            that is, inward supplies received.</p>
        <p>GSTR-7 form contains details of Tax deducted at source (TDS) under GST, the tax liability, and TDS refund.
        </p>
        <p>GSTR7 must be filed on the 10th of the subsequent month.</p>
        <h5>GSTR-7A</h5>
        <p>GSTR 7A is better known as the TDS certificate which is generated as soon as the GSTR filing in done by the
            tax deductor in Form GSTR-7. The assessee uses it for keeping records.</p>
        <h5>GSTR-8</h5>
        <p>The form GSTR-8 is filed by e-commerce businesses who collect tax at source (TCS) to furnish the details of
            the supplies made through the e-commerce platform.</p>
        <p>It is filed on the 10th of every month.</p>
        <h5>GSTR-9</h5>
        <p>The GSTR 9 form is filed annually by entities to furnish the details of all purchases, sales, input tax
            credit or refund claimed or demand created etc. GSTR-9 is filed by normal taxpayers, SEZ unit, SEZ
            developer, OIDAR service providers, and the taxpayers who have withdrawn from the composition scheme to
            normal taxpayers.</p>
        <p>Annual return purposes. It has to be duly filled by all taxpayers by the coming financial year 31st. This
            mainly consists of all the details of the 12 GSTR3 filed during the financial year.</p>
        <h5>GSTR-9A</h5>
        <p>GSTR-9A is filed by entities who have opted for composition scheme any time during the financial year. The
            details furnished in GSTR-9A are inward and outward supplies, taxes paid, demand created, refund, input tax
            credit availed or reversed.</p>
        <h4>Late Fees/Penalty For Failing To Filing The Return On Time</h4>
        <p>In case the taxpayer fails to complete GST Return filing, file the returns within the time limits, then
            he/she will be asked to pay a penalty which is also known as the late fee. The late fee will be Rs.20 per
            day if it is NIL return or only purchases and Rs 50 will be levied if we fail to furnish the sales. Thus, it
            will come around Rs.25 under the CGST and again Rs.25 under the SGST. The total amount to be paid will be
            Rs.50 per day. The maximum penalty can be Rs.5000.The IGST do not levy any late fees.</p>
        <h4>GST Return late filing</h4>
        <p>Under the GST rules, the filing of returns is mandatory. You are expected to file a Nil return in case no
            transaction has been made.
            <ul>
                <li>In case you have not yet filed the previous quarter/month return, then you will not be able to do
                    the
                    return filing process.</li>
                <li>In case of any delay in GST return, you will have to face serious penalties and fines.</li>
                <li>The GSTR 1 late filing penalty will be added in the GSTR-3B ledger of liability as soon as a delay
                    occurs.</li>
            </ul>
        </p>

