        <h4>What is GST Registration?</h4>
        <p>
        Every supplier who is making a taxable supply of goods or services or both shall register in every State/Union
        Territory from where he makes taxable supply if his aggregate turnover exceeds 20 lac (10 lac for north eastern
        states) in a financial year.

        Aggregate turnover means value of all taxable supplies (excluding value of inward supplies liable to tax on
        reverse charge basis), exempt supplies, exports of goods and services or both and inter-state supplies of
        persons having the same Permanent Account Number [PAN] to be computed on all India basis, but excludes Central
        sales tax, State tax, Union Territory Tax, Integrated Tax and Cess.
        </p>
        <h4>Who is required to have GST Registration?</h4>
        <p>
        

The following person are required to take registration under GST irrespective of turnover:
<ul>

<li>Person making interstate supplies</li> 
<li>casual taxable persons</li>
<li>Persons who are required to pay tax under RCM</li>
<li>Person who are required to pay tax under sub-section (5) of section 9</li>
<li>Non-resident taxable persons making taxable supply</li>
<li>Persons who are required to deduct tax under section 51</li>
<li>Persons who make taxable supply of goods or services or both on behalf of other taxable persons whether as an agent or otherwise</li>
<li>Input Service Distributor</li>
<li>Persons who supply goods or services or both, other than supplies specified under sub-section (5) of section 9, through such electronic commerce operator who is required to collect tax at source under section 52</li>
<li>Every E-commerce operator</li>
<li>Every person supplying OIDR services from a place outside India to a person in India, other than a registered person</li>
<li>Notified Persons</li>



 
</ul>
        </p>
