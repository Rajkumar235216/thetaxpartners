      <h2>Company Annual Filing 2019-2020</h2>
<p>Every Company registered under Companies Act is required to file their returns with the Registrar of Companies annually.
</p>
<p>Company Annual Filings refers to the filing of Audited Annual Financial Accounts of the Company along with Directors Report and Annual Return of Company with Registrar of Companies.&nbsp;These yearly filings are mandatory for every registered Company whether the Company carries on business or not.</p>
<h3>Company Annual Filing 2020</h3>
<p>Companies registered on or before 31st December 2019 are liable to file the Financial Accounts, Annual Return and Income Tax Return for the year end on 31st March 2020</p>
<p>Companies registered on or after 1st January 2020 have to file the Financial Accounts and Annual Return for the year end on 31st March 2020 ONLY.&nbsp;However, company registered on or after 1st January 2020&nbsp; and before 31st March 2020 has to file an Income Tax Return for the year ended on 31.03.2020.</p>
<h4>1. Annual Financial Accounts</h4>
<p>The annual financial statements shall be filed in accordance with the format and accounting standards prescribed and give a true and fair view of the state of affairs of the company. The company shall also consolidate the financials of its subsidiary and associates including that of foreign, if they have any.</p>
<p>The financial statement after being duly adopted shall be filed along with the Board’s Report in Form AOC-4 within 30 days of the <a href="https://companiesinn.com/articles/annual-general-meeting-agm-of-company">Annual General Meeting (AGM)</a>. However, an OPC shall file their financials by 27<sup>th</sup> September (180 days from closure of financial year). Companies falling under the ambit of secretarial audit shall attach the report in Form MR-3 along with the Board’s Report wherever applicable.</p>
<p>Certain classes of companies are required to report the financials in XBRL form. Such companies which have filed their financials in XBRL format shall continue to report in same format, though they do not fall under such class of companies in the following years.<br></p>
<h4>2. Annual Return</h4>
<p>Annual Return under Companies Act is an yearly Return containing the general particulars of company on the close of the financial year such as details of &nbsp;its registered office, business activities, particulars of its holding, subsidiary companies, shares, debentures and other securities and shareholding pattern,&nbsp; members and debenture-holders along with changes therein, promoters, directors, key managerial personnel along with changes therein,&nbsp; meetings of members or a class thereof, Board and its various committees along with attendance details, remuneration of directors and key managerial personnel, penalty or punishment imposed on the company, matters relating to certification of compliances, disclosures etc.</p>
<p>Annual Return in Form MGT-7 has to be&nbsp; filed within 60 days of the <a href="https://companiesinn.com/articles/annual-general-meeting-agm-of-company">Annual General Meeting (AGM)</a>.</p>
<h4>3. Income Tax Return</h4>
<p>As per the Income Tax Act, every company is required to close its financial year on 31st March every year and file returns with the Income Tax Department.
  </p>
<p>Every Company is required to file Income Tax Returns every year mandatorily whether they do business or not.
  If the annual turnover of a business is more than Rs.100 lakhs, the accounts have to be audited by a qualified Chartered Accountant as required under the Income Tax Act as well.</p>


