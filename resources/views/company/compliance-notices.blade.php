    <p style="text-align: justify;"><strong><u>Show Cause Notice</u></strong></p>
<p style="text-align: justify;">Certain Compliances are required to be made by Corporates as per Law. Non compliance of which might lead to receiving of Show Cause Notice as in asking why the compliance has not been made or why it was not done within the time frame. Show Cause Notice is sent on either Company’s Mail or&nbsp; at registered office through registered post or any other viable mode. Notice generally contains Ref No. , Date, Time, Section or provision which was violated, time limit to reply to the same etc. Not reverting to the same within the time limit allotted will lead to legal/penal action.</p>
<p style="text-align: justify;"><strong><em>Fundamental question that is asked by all is HOW TO REPLY TO SHOW CAUSE NOTICE RECEIVED FROM ROC/RBI/MCA etc???</em></strong></p>
<p style="text-align: justify;">There is no formula on how to respond to Show Cause Notice as each case is different. However the following things must be looked while drafting a reply to the show cause Notice:</p>
<h2 style="text-align: justify;"><strong>1. </strong><strong>What non compliance leads to show cause notice??</strong></h2>
<p style="text-align: justify;">What Issues has been raised in the Show cause Notice? Whether the Non compliance has been made? So the Very First thing to do is to understand the reason for receiving the Show Cause Notice. If you do not precisely understand the reason you will not be able to provide an effective response.</p>
<p style="text-align: justify;">For Ex: A Show Cause Notice is received by the Company from Registrar of Companies u/s 248 of <a href="https://taxguru.in/company-law/presidents-assent-companies-act-2013.html" target="_blank" rel="noopener noreferrer"><strong>Companies Act, 2013</strong></a>, that the company is not carrying on any business or operation for a period of 2 immediately preceding financial years, so the Registrar of Companies is willing to struck off the name of the company from the Registrar of Companies.</p>
<p style="text-align: justify;">Therefore, there is a need to understand what led the Registrar of Companies to believe that Company is not Carrying operation for a period of 2 immediately preceding financial years. The Reasons might be non filing of Financial Statements and Annual Return in the Form AOC-4 and Form MGT-7. Also the Company might be a dormant Company and still the company might have not filed to ROC any application within such period for obtaining the status of Dormant Company u/s 455 of Companies Act,2013.</p>
<h2 style="text-align: justify;"><strong>2. </strong><strong>Why</strong> <strong>the non compliance was done?</strong></h2>
<p style="text-align: justify;">Once you are aware of the reason for receiving the Show Cause Notice, the next step is to see if the default has been committed then WHY the default was committed and if the default has not been committed then you have to give valid proof to counter the same. This Para is the Body of the Reply which should have a valid and reasonable ground for committing the default or for countering the default if the same has not been committed. This step is basically to justify the <strong>OPPORTUNITY OF BEING HEARD.</strong></p>
<p style="text-align: justify;">For Ex: Continuing with the above mentioned example ROC had a reason to believe that the Company was not carrying any business. But as per the Company after analyzing the Show Cause Notice has a reason to believe &nbsp;was carrying on operation so the Company can reply that that the Company has been operative and carrying on its business activity and have the revenue from operations and also complying with its mandatory compliances regarding Annual Filings till 2015. Also can mention that the Company is in process of filing for remaining years. Form and Pay later Challan is also attached. Financial Statements of the Company are hereby enclosed for your reference.</p>
<h2 style="text-align: justify;"><strong>3. </strong><strong>Why </strong>action should not be taken? <strong>What</strong> are my options???Relief sought</h2>
<p style="text-align: justify;">Now after understanding the reason for getting the Notice and giving reasonable ground for committing the default or for countering the default, the next step is to look out the options available. Justification is required so that legal action is not taken against the Company. Prayer should be done so that legal action is not taken against the Company by the Concerned Department.</p>
<p style="text-align: justify;"><strong><em>Note: Time period within which reply has to be made is mentioned in the Show cause Notice. So, it is very important to do the same within the Time Frame Allotted .If the same is not done within the time frame than can lead to legal/penal consequences .For Ex. : In the Example mentioned above if reply is not made within 30 days, the registrar will struck off the Company from the Register of Members.</em></strong></p>
<p style="text-align: justify;"><strong><em>While Preparing the Reply of the Show Cause Notice one should be careful regarding the Legal Provisions of the Act and also regarding the Legal responsibility of Promoters/directors. </em></strong></p>
