<p>An Individual who is Director of the Company or the designated partner of LLP is required to have a Director Identification Number (DIN) or Designated Partner Identification Number (DPIN). The Director or Designated Partner who has been allotted DIN/DPIN are required to intimate with respect to any change in particular, change in address or any other details as mentioned in the <strong>Director Identification Number</strong> (DIN)/ Designated Partner Identification Number (DPIN), because it is not possible that the information stated while obtaining the DIN/DPIN remain the same for a lifetime. If there are changes it needs to apply for changes in DIN. Therefore in this article, we will discuss the changes in the particulars of DIN by filling the form DIR-6.</p>



<h2>Is it possible
to change the details in an existing Director Identification Number (DIN)?</h2>



<p>Yes, after obtaining the DIN/DPIN the information which has been
mentioned while obtaining the DIN/DPIN may change from time to time with
respect to an Email ID, change in address, Change in name, etc.&nbsp; Therefore the individual is required to
intimate the Central Government for any change in particulars of DIN/DPIN
details by filling a form with the Ministry of Corporate Affairs.</p>



<h2>Which form is
required to be filed for a change in details of existing Director
Identification Number (DIN)?</h2>



<p>The Form DIR-6 is required to be filed with Registrar of Companies for
change in particulars of DIN.</p>



<h2>What is the
Procedure for the Change in Director Identification Number (DIN)?</h2>



<p><em>Following are the steps mentioned
below:</em></p>



<ol><li>Download form DIR-6 from the MCA
portal.</li><li>Fill the details accordingly.</li><li>Attach the self-attested document
as proof for change in particulars:<ol><li>Proof of Identity: In the case of Indian Nationals PAN card is
mandatory and in the case of foreign nationals, Passport is mandatory.</li></ol><ol><li>Proof of residence: Bank Statement, Electricity bill, telephone bill.
(not older than 2 months)</li></ol></li></ol>



<p><strong>In the case of foreign nationals, address proof should not be older than 1 year from the date of filing of e-form.</strong></p>



<ul><li>Attach Form DIR-7.</li><li>Affix the DSC of the applicant and DSC of the professional who is certifying the Form.</li><li>Upload the Form and there are no government fees for the said form DIR-6.</li><li>After uploading, <strong>MCA</strong><sup>[1]</sup> will approve within 1 to 2 working days.</li></ul>



<p><b>Also, Read: </b> <mark style="background: #fffd03 !important;">How to Change GST Registration details of a business</mark>.</p>



<h2>Types of Changes where DIR-6 is required
to be Filed</h2>



<ul><li>Name of director/ designated
partners</li><li>Date of birth</li><li>Voter’s Identity card
number/Passport number/Driving license number/Aadhaar Number.</li><li>Email ID/Mobile no.</li><li>Permanent residential
address/Present residential address</li><li>Father’s Name</li><li>Nationality</li><li>Gender</li><li>PAN No.</li></ul>



<p><strong>For Example</strong>: If there is any spelling error
like name in the PAN card is Hiranandani while in DIN it is showing Hiranadani,
therefore the Director may proceed for such correction in DIN by filling the
Form DIR-6.</p>



<h2>What are the basis on which application can
be rejected?</h2>



<p><em>The following are the grounds on which the application is rejected:</em></p>



<ul><li>Proof of Identity has not been attested by an authorized person.</li><li>Proof of Residential address has not been arrested by authorized person.</li><li>The address is not matching with the address details mentioned in the proof attached.</li><li>Proof of Identity or proof of Residential not attached.</li><li>The supporting documents attached not valid or has expired.</li><li>The gender not entered correctly in DIN forms.</li><li>The submitted application is duplicate DIN application i.e. an approved DIN already exists in this name.</li><li>Any other reason may be given for rejecting the application depending upon the case to case.</li></ul>



<h2>Conclusion</h2>



<p>It becomes an obligation for the applicant who is holding the valid DIN/DPIN and if there are any changes in the particulars of the DIN/DPIN then it is mandatory to intimate by filling the form with the Ministry of Corporate Affairs.


