            <h2 style="text-align: justify;"><strong>What does liquidation mean?&nbsp;&nbsp;</strong><br>&nbsp;</h2>
            <div style="text-align: justify;">When <strong>starting a company in India</strong>, investors should take into
                consideration that some of the&nbsp;businesses may enter the <strong>liquidation
                    procedure</strong>&nbsp;due to a set of factors (faulty management, economic issues, low demand and
                others). According to the <strong>Indian legislation</strong>, the <strong>liquidation
                    procedure</strong> refers to the manner in which the <strong>company’s assets are
                    terminated</strong> and distributed to the entitled parties.&nbsp;<br>&nbsp;</div>
            <div style="text-align: justify;">The<strong> liquidation</strong> can be started on a<strong> voluntarily
                    basis</strong> through the intervention of the <strong>company’s creditors </strong>or other
                members. If this case will apply, the procedure can be completed without the intervention of a local
                court&nbsp;and <strong>our team of specialists in company formation in India</strong> can assist with
                further advice on the manner in which the procedure is performed.<br>&nbsp;</div>
            <h2 style="text-align: justify;"><strong>Who can start the liquidation procedure in India?&nbsp;</strong>
            </h2>
            <div style="text-align: justify;"><br>The <strong>liquidation procedure in India </strong>is initiated by
                filing a<strong> petition for insolvency</strong>. The steps for the<strong> liquidation process in
                    India </strong>are prescribed under the <strong>Insolvency and Bankruptcy Code</strong>. The initial
                steps can be taken, as mentioned above, by the <strong>company’s creditors</strong>, but they can also
                be started by the company's owners.&nbsp;It may also be started by the company’s contributors or by the
                <strong>Indian institutions</strong>.&nbsp;<br><br>In the latter case, the main institutions that can
                request this procedure are the country’s government or the <strong>Registrar of Companies</strong>. Once
                the petition was filed, it is legally required to present the respective petition in a local newspaper
                for a period of minimum 14 days. The advertisement must be in the language of the<strong> Indian
                    region</strong> where the company resides, but also in English. <strong>Our team of consultants in company registration in India</strong> can
                provide more details on other necessary steps imposed in this case.&nbsp;<br>&nbsp;</div>
            <h2 style="text-align: justify;"><strong>Voluntary liquidation in India&nbsp;&nbsp;</strong><br>&nbsp;</h2>
            <div style="text-align: justify;"><strong>Liquidation</strong> is necessary when the<strong>
                </strong>company has to <strong>pay a large amount of money as debts</strong> to various parties.
                Through the<strong> voluntary liquidation</strong>, the directors of a <strong>solvent company
                </strong>will sign a<strong> formal declaration of solvency</strong>. The directors will need to<strong>
                </strong>appoint a general board meeting in which the <strong>decision of winding up the
                    company&nbsp;</strong>will be established (it can be completed if two thirds of the members are in
                favor of this proposal).&nbsp;<br>&nbsp;</div>
            <div style="text-align: justify;">Then, the company’s representatives will need to <strong>appoint a
                    liquidator</strong>, who can handle the legal procedure and to notify the <strong>Registrar of
                    Companies</strong> on the company’s current situation (in a period of maximum 10 days since the
                resolution was signed).&nbsp;<strong>Our team of agents in company formation in India</strong> can offer
                further information on the legal aspects related to the&nbsp;<strong>voluntary
                    liquidation</strong>.&nbsp;<br>&nbsp;<h2><strong>What are the main steps of the voluntary
                        liquidation in India?&nbsp;</strong><br>&nbsp;</h2>
                <div>Although the <strong>voluntary liquidation in India </strong>represents an out-of-court procedure,
                    there are a set of steps that have to be concluded by those who have initiated the procedure.
                    <strong>Our team of specialists</strong>, who can assist investors in <strong>opening a company in India</strong>. can also provide full legal
                    advice on the steps regarding the <strong>voluntary liquidation</strong>, which generally refer to
                    the below mentioned aspects:&nbsp;<br>&nbsp;</div>
                <ul>
                    <li>the company’s management has to follow the requirements imposed under the <strong>Indian Companies Act</strong>;</li>
                    <li>thus, the majority of the company’s directors has to approve on the <strong>liquidation
                            procedure</strong>, by signing a board resolution;</li>
                    <li>the majority of the company’s shareholders also has to approve on this, by signing a special
                        resolution;</li>
                    <li>the special resolution has to be signed by at least three quarters of the company’s
                        shareholders;</li>
                    <li>the <strong>company’s creditors</strong> must also state that they <strong>agree on the
                            winding-up</strong>;</li>
                    <li>it is also required to create a <strong>declaration of solvency</strong> and to <strong>appoint
                            a liquidator</strong>, who will be in charge with making a report regarding the company’s
                        current financial situation (capital, assets and so on).&nbsp;</li>
                </ul>
                &nbsp;<h2><strong>Can a shelf company be liquidated in India?&nbsp;</strong></h2><br>Yes, the<strong> shelf
                        company</strong> (also referred to as a <strong>dormant company</strong>) can be
                <strong>shut down in India</strong>. The <strong>shelf company</strong> represents a legal entity that
                was incorporated at a given time in the past, with the purpose of being sold. This type of business
                represents a simpler way for a businessman to start a local business, as the incorporation process has
                already been completed.&nbsp;<br><br>However, such companies can also be <strong>shut down</strong>,
                provided that there is no demand for them on the local market. A <strong>dormant company in
                    India</strong>, which is also characterized by the fact that it did not enter any commercial
                activities, can be shut down as stipulated under the <strong>Companies Act 2013</strong>. Since this
                type of business did not enter commercial activities, there are no financial documents that need to be
                audited, in order to estimate its current situation.&nbsp;<br><br>Thus, the<strong> Indian government
                </strong>offers the possibility of a<strong> fast liquidation procedure</strong>. In this sense,
                the<strong> owners of a dormant company</strong> have to complete a form, the <strong>Form
                    STK-2</strong>, with the <strong>Registrar of Companies</strong>, this being the main step in order
                to <strong>complete the liquidation</strong> <strong>of the business</strong>.&nbsp;&nbsp;<br><br>The
                document will need to be signed by the company’s director and in order to benefit from this<strong>
                    faster liquidation procedure</strong> (known in<strong> India</strong> as the <strong>Fast Track
                    Exit Scheme</strong>), the company is required to be dormant (meaning, not to have any commercial
                activities) for at least a year prior to starting the <strong>liquidation</strong>.&nbsp;<br>&nbsp;<h2>
                    <strong>What is the compulsory liquidation in India?&nbsp;</strong></h2><br>The <strong>compulsory
                    liquidation procedure</strong> is also prescribed under the <strong>Insolvency and Bankruptcy Code
                    of India</strong>. The procedure can be started when one of the<strong> company’s creditor</strong>s
                requests the <strong>payment of a debt</strong> of at least INR 100,000. The<strong> creditor
                </strong>can request the beginning of the <strong>compulsory insolvency procedure </strong>at the <strong>National Company Law
                        Tribunal</strong>.&nbsp;&nbsp;<br><br>Under the<strong> Indian legislation</strong>, a
                period of 180 days is prescribed once the<strong> creditor</strong> entered his or her application with
                the <strong>National Company Law Tribunal</strong>, a period in which the<strong> recovery of the
                    assets</strong> or the<strong> enforcement procedures</strong> can’t be started. It is necessary to
                know that the 180 days period can be extended with an additional 90 days.&nbsp;&nbsp;<br><br>As a
                general rule, the <strong>liquidation procedure in India </strong>can last up to two years (it can be
                completed in two years since the application was made), but this is the case of<strong> compulsory
                    liquidation</strong>, as the <strong>voluntary liquidation</strong> can take much less
                time.&nbsp;<br>&nbsp;
