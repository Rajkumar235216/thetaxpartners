<p style="text-align: justify;">6. After doing this complete process, you can track the transaction status of your form under MCA services menu. What you need to do is just put the SRN no. generated in the challan and you will be able to know whether your form is approved or for pending for approval.</p>
<p style="text-align: justify;"><span style="color: #ff0000;"><strong>General Points to be Kept in Mind while Doing the Annual ROC Filing</strong></span></p>
<ul>
<li style="text-align: justify;">The notice of Board Meeting should be sent to all the directors before 7 days and acknowledgement for the same should be taken.</li>
<li style="text-align: justify;">As per Section 134 of the Companies Act, 2013 the financial statement, including consolidated financial statement, if any, shall be signed on behalf of the Board at least by the chairperson of the company where he is authorized by the Board or by two directors out of which one shall be the managing director and the Chief Executive Officer, if he is a director in the company, the Chief Financial Officer and the company secretary of the company, wherever they are appointed, or in the case of a One Person Company, only by one director.</li>
<li style="text-align: justify;">As per Section 101 of the Companies Act, 2013, a clear 21 days’ notice for the general meeting shall be given to all the members, legal representatives of any deceased person, auditor, and every director of the company by physical or electronic mode. The notice should also contain the location map of the venue of the general meeting as per Secretarial Standards and should be placed on the website if any.</li>
<li style="text-align: justify;">The company shall prepare its books of accounts and keep at its registered office. If the company chooses to place at any other place, then the company will have to file AOC-5 by passing a board resolution.</li>
<li style="text-align: justify;">While uploading the forms, care should be taken that the form is the latest version as provided on the MCA.</li>
</ul>

