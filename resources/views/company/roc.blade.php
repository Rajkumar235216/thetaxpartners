      <div class="fsize16"><p style="text-align: justify;"><strong><u>ROC Filing</u></strong></p>
<p style="text-align: justify;">Every company is required to file the annual accounts and annual return as per The Companies Act, 2013 within 30 days and 60 days respectively from the conclusion of the Annual General Meeting. The ROC filing of annual accounts is governed under Section 129(3), 137, of The <strong>Companies Act, 2013</strong> read with Rule 12 of the Company (Accounts) Rules, 2014 and annual return is&nbsp;<strong>governed under Section 92</strong>&nbsp;of the Companies Act,2013 read with Rule 11 of the <strong>Companies (Management and Administration) Rules, 2014.</strong></p>
<p style="text-align: justify;">The procedure of ROC filing the annual return and annual accounts can be easily understood by the following process:</p>
<p style="text-align: justify;">1. Hold a Board Meeting to</p>
<ul style="text-align: justify;">
<li>Authorize the auditor for the preparation of financial statements as per Schedule III of the Companies Act, 2013.</li>
<li>Authorize the Director or Company Secretary for preparation of Board Report and Annual Return as per the Companies Act, 2013.</li>
</ul>
<p style="text-align: justify;">2. Hold another Board Meeting for approving the draft financial statements, Board Report and Annual Return by the directors of the company.</p>
<p style="text-align: justify;">3. Conduct the Annual General meeting of the Company and pass the necessary resolutions. Please note that the financial statements are considered final only when the same is approved by the shareholders at the General Meeting.</p>
