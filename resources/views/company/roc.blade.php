<p style="text-align: center;"><strong>⇓</strong></p>
<p style="text-align: justify;">2. Fill the appropriate E-forms applicable of your company and attach the pdf or XML documents as per the requirement of the form ===&gt; Press Check form ===&gt; Attach the Digital Signature of the Director and Practicing professional (if applicable; exempted for OPC and small companies) ===&gt; Now do pre-scrutiny.</p>
<p style="text-align: center;"><strong>⇓</strong></p>
<p style="text-align: justify;">3. Get yourself registered on MCA portal as a Business User or registered user. After getting registered on the portal, login with your ID and password.</p>
<p style="text-align: center;"><strong>⇓</strong></p>
<p style="text-align: justify;">4. After login go to upload E-forms ===&gt; Browse the filled and signed form from your system’s location.</p>
