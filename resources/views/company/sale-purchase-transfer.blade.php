            <p style="text-align: justify;"><span style="color: #000000;"><strong>Step 6.</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">If the documents are in order then the board
                    shall register the transfer by passing a board resolution. If the board refuses to register the
                    transfer of shares then a notice will be served to the transferee within 30 days of receipt of such
                    share deed and an appeal can be done to the tribunal within 30 days from the date of notice served
                    by the company.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">It is to be noted that no such notice or
                    intimation is given to ROC. The share transfer details will be given to ROC in the annual return of
                    the company in e-form MGT-7.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;"><strong>Transfer of Partly Paid Up
                        Shares</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">In case of transfer of partly paid-up shares,
                    the company will give notice to the transferee in form no. SH-5 and when the transferee will give a
                    no-objection certificate to the company within 2 weeks from the date of receipt of notice after that
                    the company will register the share transfer.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;"><strong>Penalty for
                        Non-compliance</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">As per the provisions of the Act, where there
                    is a non-compliance by the company of the provisions relating to the transfer of shares, the company
                    shall be punishable with the fine not less than Twenty-Five Thousand Rupees but which may extend to
                    Five Lac Rupees and every officer in default will be punishable by a fine not less than Ten Thousand
                    Rupees but which may extend to One Lac Rupees.</span></p>


