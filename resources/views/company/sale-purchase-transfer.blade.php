            <p style="text-align: justify;"><span style="color: #000000;">According to <strong>Rule 11</strong> of
                    Companies (Share Capital &amp; Debentures) Rules 2014: Instrument of transfer.-</span></p>
            <ol style="text-align: justify;">
                <li>
                    <p><span style="color: #000000;">An instrument of transfer of securities held in physical form shall
                            be in Form SH.4 and every instrument of transfer with the date of its execution specified
                            thereon shall be delivered to the company within sixty (60) days from the date of such
                            execution.</span></p>
                </li>
                <li>
                    <p><span style="color: #000000;">In the case of a company not having a share capital, provisions of
                            sub-rule (1) shall apply as if the references therein to securities were references instead
                            to the interest of the member in the company.</span></p>
                </li>
                <li>
                    <p><span style="color: #000000;">A company shall not register a transfer of partly paid shares
                            unless the company has given a notice in Form No. SH.5 to the transferee and the transferee
                            has given no objection to the transfer within two weeks from the date of receipt of the
                            notice.</span></p>
                </li>
            </ol>
            <p style="text-align: justify;"><span style="color: #000000;"><strong><u>Procedure For Transfer Of
                            Shares</u></strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;"><strong>Step 1.</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">Check whether the Articles of Association
                    allows you for transfer of shares, if not then amend the articles by passing special resolution in
                    extra-ordinary general meeting.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;"><strong>Step 2.</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">The transferor shall give notice in writing
                    for his intention to transfer his shares to the company.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;"><strong>Step 3.</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">The company will now inform the other members
                    as regards the availability of shares and the price at which the shares would be available and will
                    quote a time limit within which the members can purchase the shares. If within the time limit no
                    member comes to purchase the shares then the shares will be offered to an outsider and if the
                    outsider accepts the shares then the company will have to accept the transfer. The price of the
                    shares is finalized by the directors or the auditors of the company.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;"><strong>Step 4.</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">Obtain the Share transfer deed in form SH-4
                    duly executed by both the transferor and transferee. The transfer deed should bear stamps according
                    to the Indian Stamp Act and Stamp Duty Notification. The present rate of transfer of shares is 25
                    Paise for every one hundred rupees of the value of shares or part. Cancel the stamps affixed at the
                    time or before the signing of the transfer deed. The signatures of both the transferor and
                    transferee must be witnessed by a person whose details like his signature, name, and address.</span>
            </p>
