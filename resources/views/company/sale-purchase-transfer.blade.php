            <p style="text-align: justify;"><span style="color: #000000;"><strong>Step 5.</strong></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">Share certificate or allotment letter needs to
                    be attached to the share transfer deed and deliver it to the company. The share transfer deed needs
                    to be deposited with the company within 60 days from the date of such execution by or on behalf of
