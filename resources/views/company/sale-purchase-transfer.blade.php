                            sizes="(max-width: 372px) 100vw, 372px" width="372" height="223"></a></span></p>
            <p style="text-align: justify;"><span style="color: #000000;">According to <strong>section 56</strong> of
                    the Companies Act,2013 a company shall not register a transfer of securities of the company , or the
                    interest of a member in the company in the case of a company having no share capital, other than the
                    transfer between persons both of whose names are entered as holders of beneficial interest in the
                    records of a depository, unless a proper instrument of transfer, in such form as may be prescribed,
                    duly stamped, dated and executed by or on behalf of the transferor and the transferee and specifying
                    the name, address and occupation, if any, of the transferee has been delivered to the company by the
                    transferor or the transferee within a period of sixty days from the date of execution, along with
                    the certificate relating to the securities, or if no such certificate is in existence, along with
                    the letter of allotment of securities:</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">Provided that where the instrument of transfer
                    has been lost or the instrument of transfer has not been delivered within the prescribed period, the
                    company may register the transfer on such terms as to indemnity as the Board may think fit.</span>
            </p>
            <p style="text-align: justify;"><span style="color: #000000;">(2) Nothing in sub-section (1) shall prejudice
                    the power of the company to register, on receipt of an intimation of transmission of any right to
                    securities by operation of law from any person to whom such right has been transmitted.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">(3) Where an application is made by the
                    transferor alone and relates to partly paid shares, the transfer shall not be registered, unless the
                    company gives the notice of the application, in such manner as may be prescribed, to the transferee
                    and the transferee gives no objection to the transfer within two weeks from the receipt of
                    notice.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">(4) Every company shall, unless prohibited by
                    any provision of law or any order of Court, Tribunal or other authority, deliver the certificates of
                    all securities allotted, transferred or transmitted—</span><br><span style="color: #000000;">(a)
                    within a period of two months from the date of incorporation, in the case of subscribers to the
                    memorandum;</span><br><span style="color: #000000;">(b) within a period of two months from the date
                    of allotment, in the case of any allotment of any of its shares;</span><br><span
                    style="color: #000000;">(c) within a period of one month from the date of receipt by the company of
                    the instrument of transfer under sub-section (1) or, as the case may be, of the intimation of
                    transmission under sub-section (2), in the case of a transfer or transmission of
                    securities;</span><br><span style="color: #000000;">(d) within a period of six months from the date
                    of allotment in the case of any allotment of debenture:</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">Provided that where the securities are dealt
                    with in a depository, the company shall intimate the details of allotment of securities to
                    depository immediately on allotment of such securities.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">(5) The transfer of any security or other
                    interest of a deceased person in a company made by his legal representative shall, even if the legal
                    representative is not a holder thereof, be valid as if he had been the holder at the time of the
                    execution of the instrument of transfer.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">(6) Where any default is made in complying
                    with the provisions of sub-sections (1) to (5), the company shall be punishable with fine which
                    shall not be less than twenty-five thousand rupees but which may extend to five lakh rupees and
                    every officer of the company who is in default shall be punishable with fine which shall not be less
                    than ten thousand rupees but which may extend to one lakh rupees.</span></p>
