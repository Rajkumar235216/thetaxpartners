            <p style="text-align: justify;"><span style="color: #000000;">Shareholders are known as the owner of a
                    Private Limited Company. If a company wants to introduce new investors or any shareholder wants to
                    transfer his/her ownership then the shares of the private limited company need to be transferred. A
                    Private Limited Company cannot invite the public to subscribe to any securities of the
                    company.</span></p>
            <p style="text-align: justify;"><span style="color: #000000;">For transfer of shares provisions from the
                    following resources are considered:-</span></p>
            <ol style="text-align: justify;">
                <li>
                    <p><span style="color: #000000;">Section 56 of the Companies Act,2013</span></p>
                </li>
                <li>
                    <p><span style="color: #000000;">Rule 11 of Companies ( Share Capital &amp; Debentures ) Rules
                            2014</span></p>
                </li>
                <li>
                    <p><span style="color: #000000;">Provisions given in model articles of association given in Table
                            ‘F’ of Schedule- 1</span></p>
                </li>
            </ol>
            <p style="text-align: justify;"><span style="color: #000000;"><a style="color: #000000;"
                        href="https://taxmantra.com/wp-content/uploads/2018/02/Transfer-of-Shares1.jpg"><img
