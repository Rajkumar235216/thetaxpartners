<span style="font-weight: 400;">i.&nbsp; A &nbsp;Digital Signature Certificate(DSC)</span><br>

<span style="font-weight: 400;">ii.&nbsp; A Director Identification Number (DIN)</span><br>

<span style="font-weight: 400;">iii.&nbsp; Registration on the MCA Portal or New user registration</span><br>

