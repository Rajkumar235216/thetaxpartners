&nbsp;
<h2 id="What"><strong>What are the types of business structures in India?</strong></h2><br>
<span style="font-weight: 400;">Let’s try and understand the types of business structures available in India. Here is a
    list of some of them:</span><br>
<h3><b>1. One Person Company (OPC)</b></h3><br>
<span style="font-weight: 400;">Recently introduced in the year 2013, an OPC is the best way to start a company if there
    exists only one promoter or owner. It enables a sole-proprietor to carry on his work and still be part of the
    corporate framework.</span><br>
<h3><b>2. Limited Liability Partnership (LLP)</b></h3><br>
<span style="font-weight: 400;">A separate legal entity, in an LLP the liabilities of partners are only limited only to
    their agreed contribution.</span><br>
<h3><b>3. Private Limited Company (PLC)&nbsp;</b></h3><br>
<span style="font-weight: 400;">A company </span><span style="font-weight: 400;">in the eyes of
    the law is regarded as a separate legal entity from its founders &nbsp;It has shareholders (stakeholders) and directors
    (company officers). Each individual is regarded as an employee of the company. </span><br>
<h3><strong>4. Public Limited Company (PLC)</strong></h3><br>
<span style="font-weight: 400;"> A PLC is a voluntary association of members which is incorporated under company law. It
    has a separate legal existence and the liability of its members are limited to shares they hold.</span><br>

<span style="font-weight: 400;">You can choose what business structure suits your business needs best and accordingly
    register your business. </span><br>

