        <h3><strong>What is TDS Return?</strong></h3>
        <p>Apart from depositing the tax, the deductor should also file a TDS return.</p>
        <p>TDS return is a quarterly statement to be given to the I-T department. It is compulsory for deductors to
            submit a TDS return on time. The details required to file TDS returns are:</p>
        <ul>
            <li><a href="https://www.paisabazaar.com/pan-card/">PAN</a> of the deductor and the deductee</li>
            <li>Amount of tax paid to the government</li>
            <li>TDS challan information</li>
            <li>Others, if any</li>
        </ul>
