        <h3><strong>Procedure for filing Revised Return</strong></h3>
        <p>The following are the different types of corrections that are to be made in order to submit an error-free TDS
            return:</p>
        <ul>
            <li>C1 correction: Under this type, you can fill in the correct details of the deductor like the name and
                address of the deductor</li>
            <li>C2 correction: Under this type, you can update challan details which include specific details like
                challan amount, BSR code, serial number of the challan, tender date of the Challan, etc.</li>
            <li>C3 correction: Under this, you can add, change or update details of the deductee</li>
            <li>C4 Correction: Under this type, you can add or delete salary details erstwhile mentioned</li>
            <li>C5 correction: Under this type, the PAN number of the employee or the deductee can be edited</li>
            <li>C9 correction: Under this type, you can insert a completely new challan and then put in the essential
                deductees</li>
        </ul>
        <p>The above mentioned charges would also be required to be again paid in case a revised return is filed by the
            deductor.<br> Revised Return can be filed multiple times to incorporate any changes.</p>
