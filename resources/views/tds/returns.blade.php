        <h3><strong>TDS Return filing Process</strong></h3>
        <p>The following points are required to be considered to make sure that an error-free TDS return is submitted :
        </p>
        <ul>
            <li>Form 27A contains a control chart whose all columns must be filled. This form is then verified in hard
                copy form with the e-TDS return filed electronically.</li>
            <li>The totals of the amount paid and the tax deducted at source have to be correctly filled and the same
                has to be filled in all the forms, including Form No. 27A, Form No. 24, Form No. 26 and Form No. 27.
            </li>
            <li>The assessees are required to mention their Tax Deduction Account Number (TAN) in Form No. 27A. This is
                similar to what is done in case of e-TDS return. This is dictated by ‘sub-section (2) of section 203A of
                the I-T Act in India’.</li>
            <li>At the time of filing the TDS return, ensure that details relating to the depositing of tax deducted at
                source have been mentioned accurately.</li>
            <li>The basic form that has been used for e-TDS return recommended by the department is compulsory to
                follow. This is because it brings consistency and better understanding in filling the forms. It is
                necessary to mention the Bank Branch Code or the BSR code. It is a 7-digit code provided to the banks by
                the Reserve Bank of India.</li>
            <li>E-TDS return has to be filed in the ASCII clean text format. To avail this format, you can use software
                of your choice such as Computex, MS Excel or Tally. Also, you have an option of using the software
                available at NSDL website known as Return Prepare Utility (e-TDS RPU Light) for filing the return
                online. It is important to ensure that the online TDS file formats come with ‘txt’ as the filename
                extension.</li>
            <li>The physical returns are submitted at any TIN-FC’s managed by NSDL. TIN-FC’s are found at specified
                areas across the country.</li>
            <li>If returns are filed online, then they can be submitted directly at NSDL TIN website. In this case, the
                deductor has to sign the return through digital signature.</li>
            <li>While submitting the return, if all the information mentioned is accurate then a provisional
                receipt/token number would be issued. This provisional receipt/token number is considered as an
                acknowledgment, stating the fact that the return has been filed. In case, the return is not accepted,
                then a non-acceptance memo will be issued along with the reasons for rejections.</li>
        </ul>
        <h3><strong>Validation of the TDS Return File</strong></h3>
        <p>The procedure for the validation of TDS return file is given below:</p>
        <ul>
            <li>Fill in the required details in the file</li>
            <li>After filling in the details, update it in the portal validation utility tool</li>
            <li>The tool is available on NSDL website for free</li>
            <li>In case any error is found in the file, FVU will provide a report for the same</li>
            <li>Make the necessary changes and verify the file again through the FVU</li>
        </ul>
        <h3><strong>Penalty for delay in filing TDS Return</strong></h3>
        <p>According to Section234E, if an assessee fails to file his/her TDS Return before the due date, a penalty of
            Rs 200 per day shall be paid by the assessee until the time the default continues. However, the total
            penalty should not exceed the TDS amount.</p>
        <h3><strong>Non-filing of TDS Return</strong></h3>
        <p>If an assessee has not filed the return within 1 year from the due date of filing return or if a person has
            furnished incorrect information, he/she shall also be liable for penalty. The penalty levied should not be
            less than Rs 10,000 and not more than Rs 1,00,000.</p>
        <h3><strong>TDS Return Preparation Utility</strong></h3>
        <p>Deductors/collectors are required to prepare e-TDS/TCS statements as per these file formats using, NSDL
            e-Gov. Return Preparation Utility or in-house software or any other third party software and submit the same
            to any of the TIN-FCs established by NSDL e-Gov. NSDL e-Governance has developed a software called e-TDS
            Return Preparation Utility (RPU) to facilitate preparation of e-TDS returns. Users must pass the e-TDS/ TCS
            return file generated using RPU through the&nbsp;File Validation Utility (FVU)&nbsp;to ensure format level
            accuracy of the file. This utility is also freely downloadable from NSDL e-Gov TIN website.</p>
