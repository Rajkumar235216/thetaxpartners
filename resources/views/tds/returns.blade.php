        <h3><strong>TDS Refund</strong></h3>
        <p>TDS is the tax amount deducted at the time of payment. At the year end, while assessing the total tax
            liability, there is a difference between the total tax deducted during the year and the actual tax
            liability. If the tax deducted at the source is less than the actual tax liability, then the difference
            between the two has to be paid by the assessee. On the other hand, if the tax deducted at source is more
            than the actual tax liability, it results in TDS refund.</p>
        <h4><strong>Status of TDS Refund</strong></h4>
        <p>The status of TDS refund can be verified in the following ways:</p>
        <ul>
            <li>An acknowledgment is sent to your registered e-mail address</li>
            <li>You can use your PAN on the website <a href="https://incometaxindiaefiling.gov.in/"
                    rel="nofollow">https://incometaxindiaefiling.gov.in/</a></li>
            <li>You can also call at CPC Bangalore on 1800-4250-0025 (toll-free number) to check the status​</li>
        </ul>
        <h4><strong>TDS Refund period</strong></h4>
        <p>The excess TDS paid by the assessee gets refunded. The time period of TDS refund amount depends on whether
            you have filed your income tax return on or before the due date or not. If you have filed it on time, then
            excess TDS amount will be refunded between three to six months.</p>
        <h4><strong>Interest on TDS Refund</strong></h4>
        <p>According to Section 200A of the I-T Act, 1961, if the income tax department does not pay the TDS refund
            amount within the specified time period, they will have to pay an interest of 6% p.a. on the refund amount.
            This interest is calculated from the first month i.e. April of any financial year. However, no interest is
            applicable if the TDS refund amount is less than 10% of actual tax liability.</p>
        <h3><strong>Benefits of TDS</strong></h3>
        <p>Filing TDS return is mandatory as per I-T Act, 1961. Some of its benefits are:</p>
        <ul>
            <li>It helps in regular collection of taxes</li>
            <li>Ensures a flow of regular income to the government</li>
            <li>Reduces the burden of lump-sum tax payment. It helps in spreading the entire tax payment over a number
                of months which makes it easier for the taxpayer</li>
            <li>Offers an easy mode of tax payment to the payer!</li>
        </ul>


