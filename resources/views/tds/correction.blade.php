      <strong><b>What is the need of Correction?</b></strong></p>
<p style="text-align: justify;"><strong><em><b><i>The following are the some of the reasons behind for </i></b></em><a href="https://taxguru.in/income-tax/file-tds-correction-statements-traces-fy-prior-fy-201112.html" target="_blank" rel="noopener noreferrer"><em><i>TDS corrections.</i></em></a></strong></p>
<p style="padding-left: 40px; text-align: justify;">&gt; Wrong PAN Number for Employee or Dedcutee update in TDS Statement</p>
<p style="padding-left: 40px; text-align: justify;">&gt; Short Deduction or Excess deduction of TDS amount</p>
<p style="padding-left: 40px; text-align: justify;">&gt; Excess or Short utilization of Challan amount</p>
<p style="padding-left: 40px; text-align: justify;">&gt; Mentioning Wrong details for LDC Number or LDC rate.</p>
<p style="padding-left: 40px; text-align: justify;">&gt; Desire to Change in Date of Deduction or Date of credit for TDS amount.</p>
<p style="padding-left: 40px; text-align: justify;">Mentioning Wrong details for Challan.</p>
<p style="text-align: justify;"><strong><em><b><i>TDS Correction can be done in two ways</i></b></em></strong></p>
<p style="padding-left: 40px; text-align: justify;">√ Online</p>
<p style="padding-left: 40px; text-align: justify;">√ Offline</p>
<p style="text-align: justify;"><strong><b>Steps for TDS correction return filing</b></strong></p>
<p style="padding-left: 40px; text-align: justify;"><strong>1. </strong><u>Check the defaults </u>raised for the quarter after the regular statement is filed. Checking can be possible through Quarterly Statement Status after 3 working days of return filing.</p>
<p style="padding-left: 40px; text-align: justify;"><strong>2.</strong> To know on the actual defaults that are raised, <u>request and download the Justification Report </u>from TRACES.</p>
<p style="padding-left: 40px; text-align: justify;"><strong>3.</strong> Read the Justification Report and <u>identify the defaults </u>that are raised for the statement.</p>
<p style="padding-left: 40px; text-align: justify;"><strong>4.</strong> On knowing the defaults, select the type of correction to be filed.</p>
<p style="text-align: justify;">The types of correction return filing are:</p>
<p style="padding-left: 40px; text-align: justify;"><b></b><strong><b>a. Online Correction</b></strong>: Correction filed through TRACES</p>
<p style="padding-left: 40px; text-align: justify;"><b></b><strong><b>b. Offline Correction</b></strong>: Correction filed through Software</p>
<p style="text-align: justify;"><strong><b>Online correction</b></strong></p>
<p style="text-align: justify;"><em><i>The steps to file online correction is given in the following:</i></em></p>
<p style="padding-left: 40px; text-align: justify;"><b></b><strong><b>a. Request for online correction: </b></strong>Place the request for online correction through the Request online correction option given in TRACES login.</p>
<p style="padding-left: 40px; text-align: justify;"><b></b><strong><b>b. Do the required changes: </b></strong>Do the relevant corrections as identified in the Justification Report.</p>
<p style="padding-left: 40px; text-align: justify;"><b></b><strong><b>c. Submit the correction: </b></strong>After completing the process of correction, submit the corrections for final processing by TRACES.</p>
<p style="padding-left: 40px; text-align: justify;"><b></b><strong><b>d. Track the correction filed: </b></strong>Once you file the correction, check the status of the statement filed for any defaults.</p>
<p style="text-align: justify;"><strong><b>Offline correction</b></strong></p>
<p style="text-align: justify;">All types of corrections except Addition of New challan can be done through offline correction. This mode of correction is generally used when the <a href="https://taxguru.in/income-tax/digital-signature-supported-kyc-validation.html" target="_blank" rel="noopener noreferrer"><strong>digital signature</strong></a> is not registered in TRACES login for doing online correction.</p>
<p style="text-align: justify;"><em><i>The steps to file offline correction is as given in the following:</i></em></p>
<p style="padding-left: 40px; text-align: justify;">a. The consolidated statement or the <u>Conso file has to be requested and then downloaded </u>from the TRACES website</p>
<p style="padding-left: 40px; text-align: justify;">b. The Conso file downloaded from TRACES has to be <u>Import to the software </u>used for correction return filing like Winman or Saral TDS etc.</p>
<p style="padding-left: 40px; text-align: justify;">c. <u>Do the relevant corrections </u>as identified in the Justification Report.</p>
<p style="padding-left: 40px; text-align: justify;">d. <u>Generate the output files </u>required to be filed or be uploaded to the ITD website.</p>
<p style="padding-left: 40px; text-align: justify;">e. The <u>filing </u>of correction return will be done similar to the filing of regular return filing.</p>
<p style="padding-left: 40px; text-align: justify;">f. Once you file the correction, <u>check the status </u>of the statement filed for any defaults.</p>
<p style="text-align: justify;"><strong><em><b><i>Points to be know :</i></b></em></strong></p>
<p style="padding-left: 40px; text-align: justify;">&gt; You can file Correction return any number of times and there is no time limit for correction return filing</p>
<p style="padding-left: 40px; text-align: justify;">&gt; Filing of online correction is not possible if the regular statement previously filed as a paper return.</p>
<p style="padding-left: 40px; text-align: justify;">&gt; If the TAN in the statement filed is wrong, then contact the concerned Assessing officer for correction in TAN</p>
<p style="padding-left: 40px; text-align: justify;">&gt; Addition of new challan correction can be possible only through online correction mode.</p>
