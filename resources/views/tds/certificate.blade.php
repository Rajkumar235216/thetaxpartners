                <p>If you are wondering how to fill the Form 16, it is essential to note that it is not to be filled up
                    by an employee. Form 16 is pre-filled based on an employee’s income and the tax deducted during the
                    year. It is generated by the employer from the TRACES software (an Income Tax Department tool). The
                    employee can check Form 16 entries or details such as allowances, perquisites or other income and
                    deductions reported by him. If there is an incorrect figure, the same can be rectified through the
                    employer.</p>
                <h2>Who are eligible for Form 16?</h2>
                <p>Every salaried individual who has a taxable income and tax has been deducted at source by the
                    employer is eligible to receive the Form 16. If the employee has an annual income less than the
                    basic exemption limit, the employer is not obliged to issue Form 16. But, if the employer has
                    deducted TDS on employee’s monthly income, then Form 16 needs to be provided to the employee.</p>
                <h2>Is Form 16 mandatory for salaried employees?</h2>
                <p>Form 16 is to be mandatorily provided by the employer to the employee if the former has deducted TDS
                    on the latter’s income, as Form 16 is a tax certificate that carries full details of income earned
                    and TDS deducted during the financial year. Form 16 helps a salaried employee to file an income tax
                    return (ITR), especially when the employee does not have income from any other sources.</p>
                <h2>How do I get Form 16? Can I get Form 16 online?</h2>
                <p>Download Form 16 for salaried employees: Form 16 can be downloaded from the TRACES portal by the
                    employer before handing it over to the employee. A detailed break-up of the allowances and
                    perquisites received by the employee along with the Standard Deduction needs to be shown in Form 16.
                    Form 16 has two parts – One is Form 16A, and the other is Form 16B.</p>
                <h2>What is Form 16A for salaried employees? Form 16A components:</h2>
                <p><em>Part A of Form 16 gives the summary of tax collected on behalf of the employee from the salary
                        income and deposited in the government account. Following are the components of Form 16A:</em>
                </p>
                <ul>
                    <li>Personal information of the employee as well as the employer: For instance, the name of the
                        employer and the individual, address details, PAN details of both (individual and employer)
                        along with the employer’s TAN details. TAN is the number assigned to an account responsible for
                        the deduction and collection of tax.</li>
                    <li>Assessment Year or AY: This refers to the year in which the income is getting assessed, which
                        means it is the year in which the taxpayer needs to work on the tax return processes. For
                        instance, in the case of income earned between 1 April 2016 and 31 March 2017, the Assessment
                        Year will be 2017-18.</li>
                    <li>Time period: The period for which the taxpayer was employed with the employer/organization in
                        the concerned Financial Year.</li>
                    <li>Summary of the salary paid to the employee</li>
                    <li>The date on which the tax was deducted from the salary</li>
                    <li>The date on which tax deposit in the account of the government is made</li>
                    <li>Summary of tax deducted and deposited quarterly with the Income Tax Department</li>
                    <li>Acknowledgement Number of the TDS Payment</li>
                </ul>
                <h2>What is Form 16B? Form 16B components:</h2>
                <p><em>Form 16 Part B is a consolidated statement covering details regarding any other income as
                        disclosed by the employee to the organization, salary paid, amount of tax paid, and tax due, if
                        any. Following are the components of Form 16B:</em></p>
                <ul>
                    <li>Salary Received: This part shows the total salary structure of an employee. Salary is further
                        divided into various components such as Leave Travel Allowance (LTA), Leave Encashment, House
                        Rent Allowance (HRA), Gratuity, and others.</li>
                    <li>Exemptions Allowed: These allowances are given to employees, as per section 10 of the Income Tax
                        Act, 1961, for Conveyance, HRA, and Children education. Hostel expenditure, medical, etc. are
                        also mentioned in Form 16B.</li>
                    <li>Deductions From Salary: The maximum limit under this is Rs 1.5 lakh. Under Section
                        80C/80CCC/80CCD, contributions made towards investments such as Public Provident Fund, Pension
                        Plans, Life Insurance policies, Tax Saving Mutual Funds, Sukanya Samriddhi Scheme, among others,
                        are included. Deductions under other sections, such as Section 80D and Section 80E are also
                        included.</li>
                    <li>Gross Income: Gross income is the sum of the total salary received from the employer and any
                        other income declared by the employee, which includes income earned from house or property etc.
                        During the phase of investment proof submission, details regarding other income need to be
                        shared by the employee with the employer.</li>
                    <li>Net Taxable Salary: Total deductions are reduced from the gross income to calculate the taxable
                        income. The tax liability of a taxpayer is calculated on this amount.</li>
                    <li>Education Cess and surcharges, rebate under Section 87, relief under Section 89 are also
                        included.</li>
                    <li>The total amount of tax payable on income, tax deducted and the balance tax due or refund
                        applicable are also mentioned</li>
                </ul>


