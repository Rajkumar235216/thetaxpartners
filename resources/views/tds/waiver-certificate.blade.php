        <h2 id="intro"><strong>1. Introduction</strong></h2>
        <p><span style="font-weight: 400;">Considering tendency of taxpayers to adopt tax evasion measures, Income tax
                provisions provide for deduction of tax at source / collection of tax source. Tax rates for such
                deduction are provided under Section 192, Section 194 and 195(non-residents). Person responsible for
                making the payment is entrusted with the responsibility of deducting the tax at specified rates either
                at the time of credit in the books or payment to the recipient, whichever is earlier and only pay the
                balance amount to the recipient. This ensures tax is collected in advance, checks tax evasion and also
                helps track income of recipients in the future.</span></p>
        <p><span style="font-weight: 400;">However, this scheme of deducting tax at source itself may create hardship to
                few taxpayers who may not be have a taxable income at all. Such scenarios could arise where:</span></p>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Taxpayer has incurred a loss for the current
                    year;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Taxpayer has carried forward losses of
                    previous years available for set off in the current year;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Taxpayer is eligible to claim certain
                    exemptions or deductions during the year;</span></li>
        </ul>
        <p><span style="font-weight: 400;">The above could result in the taxpayer not having any taxable income at all
                for the year. While TDS rates are determined in general considering larger income population as a whole
                and income category, it might lead to undue difficulties to certain taxpayers as above who would not
                have any taxable income yet tax gets deducted at source for them which they end up claiming as refund.
                No doubt these taxpayers are eligible for an interest on such refund, funds unnecessarily get blocked
                till refund is received. Moreover, they have to go through the process of filing their return to claim
                it (in a case where it was not otherwise mandatory for them to file it under law).</span></p>
        <p><span style="font-weight: 400;">Therefore, with an objective to remove this undue hardship on such taxpayers,
                income tax law provides for an option to obtain a certificate from the Assessing officer confirming
                either a lower rate of TDS compared to the rate specified under the law or a NIL rate of TDS, depending
                on facts and circumstances of each case based on the application made. Section 197 governs these
                provisions. In this article we will discuss provisions on applying for a certificate for lower deduction
                of TDS.</span></p>
        <h2 id="covered"><strong>2. Income Covered Under Section 197</strong></h2>
        <p><span style="font-weight: 400;">Section 197 application can be made by the recipient of income in case of the
                following category of receipts where TDS is required to be made under the following Sections:</span></p>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 192 – Salary income</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 193 – Interest on securities</span>
            </li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194 – Dividends</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194A – Interest other than interest on
                    securities</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194C – Contractors income</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194D – Insurance commission</span>
            </li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194G – Commission/remuneration/prize
                    on lottery tickets</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194H – Commission or brokerage</span>
            </li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194-I – Rent</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194J – Fee for Professional or
                    technical services</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194LA – Compensation on acquisition of
                    immovable property</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194LBB – Income in respect of units of
                    investment fund</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 194LBC – Income in respect of
                    investment in securitization trust</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Section 195 – Income of non residents</span>
            </li>
        </ul>
        <h2 id="eligible"><strong>3. Eligibility for Making an Application Under Section 197</strong></h2>
        <p><span style="font-weight: 400;">Application can be made where income of any person attracts TDS as per above
                mentioned sections and income of the recipient justifies non-deduction or lower deduction of income tax
                based on his estimated final tax liability.</span></p>
        <h2 id="time"><strong>4. Timeline for Making the Application</strong></h2>
        <p><span style="font-weight: 400;">Income-tax provision does not provide for a deadline to make an application
                under Section 197. However, as TDS is made on income of on-going financial year it is advisable to make
                an application at the beginning of financial year in case of regular income throughout the financial
                year and as and when the need arises in case of one-off incomes.</span></p>
        <h2 id="procedure"><strong>5. Procedure for Making the Application Under Section 197</strong></h2>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">An application for nil/lower deduction of TDS
                    using the FORM 13 is required to be filed with the Assessing Officer(TDS) for seeking permission.
                    Such Form 13 can be filed either online or manually. Regions of Mumbai, Tamil Nadu and Karnataka
                    have enabled online filing of Form 13 f</span><span style="font-weight: 400;">or faster processing
                    of applications for issue of certificates of lower/nil deduction of tax at source u/s 197(1) of
                    Income Tax Act 1961</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Suggested that the tax payers file complete
                    and correct details required for processing the application in the first instance.</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">If the application satisfies the AO, he would
                    process the issue of the certificate;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">The copy of this certificate can be attached
                    to the invoice given to the deductor, and he can use this to justify the lower tax deduction.</span>
            </li>
        </ul>
        <p><span style="font-weight: 400;">Online application made be made by logging in to TRACES or by registering
            </span><a href="http://blrtds.in/tds/signup"><span style="font-weight: 400;">here</span></a><span
                style="font-weight: 400;"> for taxpayers in Karnataka. Stepwise procedures along with screenshots is
                available </span><a href="http://blrtds.in/userfile/user_file.pdf"><span
                    style="font-weight: 400;">here</span></a><span style="font-weight: 400;">.</span></p>
        <h2 id="validity"><strong>6.Validity of an Application Made Under Section 197</strong></h2>
        <p><span style="font-weight: 400;">Section 197 is issued for a particular financial year and stands valid from
                the date of issue and throughout the financial year unless cancelled by the assessing officer (TDS)
                before the expiry.</span></p>
        <h2 id="docs"><strong>7. Documents to Submitted with Form 13</strong></h2>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Signed Form 13</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Copies of return of income along with
                    enclosures and acknowledgment for previous 3 financial years</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Copies of assessment orders for previous 3
                    financial years</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">In case of assessee having business or
                    profession income, copies of financial statement along with audit report if any for previous 3
                    financial years</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Projected profit and loss account for the
                    current financial year</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Computation of income statement for previous 3
                    financial years and estimated computation for the current financial year</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Copy of PAN card</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Tax Deduction Account Number of all parties
                    responsible for paying you</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">E-TDS return acknowledgment for previous 2
                    financial years</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Estimated income during financial year</span>
            </li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Any other documents depending on nature of
                    income</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">TDS default earlier</span></li>
        </ul>
        <p><span style="font-weight: 400;">Once the application complete in all aspects is submitted to jurisdictional
                assessing officer (TDS), application shall be disposed of within 30 days from the end of the month in
                which such application is received. &nbsp;Assessing Officer will review the documents/information
                submitted and may ask for further queries and documents before issuing the certificate/rejecting the
                application.</span></p>


