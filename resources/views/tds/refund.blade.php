      <h2 id="tds-refund">What is TDS Refund?</h2><p>
TDS refund arises when the tax amount deducted is higher than the actual tax liability.Many a times it is seen that investment projections declared during the start of a financial year do not match with the actual investments made at the end of that year.</p><p>If there is a mismatch between the total tax deducted at the end of a financial year and the income tax you are supposed to pay for that particular year, a TDS refund arises.</p>
<h2 id="claim-tds-refund">How to claim TDS Refund?</h2>
<ul>
   <li><strong>In case your employer deducts more tax than applicable to you as per your IT return filing</strong></li>
      <p>As discussed in the example above, a mismatch between tax deducted by employer and the actual tax payable is taken care of when you file your <a href="/tax/income-tax-return.html" target="_blank">IT return</a>. When you file your income tax return, you are supposed to quote your bank name and <a href="/ifsc-code.html" target="_blank">IFSC code</a>. This makes it easier for the income tax department to return the excess tax you've paid.</p>
   </ul>
<p class="bg-yellow border-yellow pad-10"><strong><ins>Quick Tip:</ins></strong>For any financial year, if you're sure that the TDS deducted will surely be more than the total tax payable by you, then under section 197 you can file Form 13 in advance for lower or nil TDS deduction. The response certificate received by income tax officer can then be submitted to the authority who is supposed to deduct TDS for you.</p>
<ul>
<li><strong>If your income is below the tax slab and your bank deducts tax on your fixed deposit.</strong></li>
   <p>In case your income does not fall under the income tax bracket and your bank has deducted tax on your fixed deposit interest, you can recover the tax amount in two ways.</p>
   <ol>
      <li> First is to declare it in your IT return form and the income tax department will automatically compute the refund and credit it to your bank account. </li>
      <li>Second way is to fill <a href="/tax/form-15g.html" target="_blank">form 15G </a> and submit it in your bank telling them that your salary is below tax slab and hence no tax should be levied on it.</li>
   </ol>
   <li><strong>If your are a senior citizen with fixed deposit accounts:</strong></li>
   <p>Senior citizens are exempt from tax deduction on interest earned on fixed deposits. If you are above 60 years of age and have fixed deposit accounts, you are required to fill and submit form 15H to ensure that bank does not deduct income tax on the FD interest earned. </p>
   <p>Alternatively, you can get the refund credited to your bank account when you file your IT return. The IT department then calculates the tax applicable to you and adjusts the excess tax paid and credits it back to the bank account you've mentioned in your IT return form.</p>
</ul>
   <p class="bg-yellow border-yellow pad-10"><ins><strong>Quick Tip: </strong></ins>When you declare your interest income from fixed deposits at the time of maturity, you declare it as a lump-sum amount. This can result in a hefty tax amount payable and also a higher tax slab (as your income goes up over a period of time). Hence, it is prudent to declare interest income yearly rather than at the time of deposit maturity.</p>

<h2 id="tds-refund-example">TDS Refund Example</h2>
<p>Sandeep works at an MNC in Bangalore. Last year he was late submitting his documents for LIC premium exemption under <a href="/tax/deductions-under-80c.html" target="_blank">section 80C</a>. As a result, his company deducted around Rs.10,000 extra as TDS.</p><p>Total tax payable by Sandeep for year 2013-2014 = Rs.30,000</p>
<p>Tax deducted by employer from Sandeep's salary = Rs.40,000</p>
<p>Tax refund Sandeep is eligible for = Rs.40,000 - Rs.30,000 = Rs.10,000</p>
<p>His total income tax paid for last year turned out to be Rs.30,000 when actually it should have been just Rs.20,000. He ended up paying extra because he could not get his LIC premium receipts on time.</p>
<p>Similarly, Arun could not invest the Rs.40,000 within the timeline set by his employer. He just could not decide on whether to get a <a href="/fixed-deposit/top10-fd-rates-for-5years.html" target="_blank">long-term fixed deposit</a> or avail a life insurance policy. While he picked his brain about this decision, he missed the cut-off date for tax proof submissions as set by his employer. Eventually he ended up paying more tax even when he did invest that amount before the financial year closed.</p><p>These are typical situations that are faced by a lot of people almost every financial year. The only way to get back this extra tax is to file your <a href="/tax/income-tax-return.html" target="_blank">income tax return</a>. The sooner you file your income tax return, the faster the returns are processed.</p>
<h2 id="tds-refund-status">TDS Refund Status</h2>
<p>You can check the TDS refund status by the following methods:</p>
<ul>
<li>An acknowledgment e-mail sent to your registered e-mail address</li>
<li>By using the PAN card number on the website - https://incometaxindiaefiling.gov.in/</li>
<li>By calling CPC Bangalore on 1800-4250-0025 (toll-free number) in order to check the status.</li>
</ul>
<h2 id="tds-refund-period">TDS refund period</h2>
<p>The TDS refund period depends on various factors. If you have filed the ITR details on time, it can take anywhere between three to six months to receive the refund. The refund period will also depend on completing the e-verification. If you do not receive your refund on time, you may check with your employer to verify the Form 16, contact your Income Tax Office, or get in touch with Ombudsman - Income Tax Department. </p>
<p>Please refer to https://incometaxindiaefiling.gov.in for more information related to ITR or TDS refund.</p>

<h2 id="interest-on-tds-refund">Interest on TDS Refund</h2>
<p>Under Section 200A of the <a href="/tax/income-tax-act.html" target="_blank">Income Tax Act</a>, if the income tax department is late in paying you the tax refund applicable to you, then you are entitled for a simple interest of 6% p.a. on your refund amount. This interest starts accruing from the first month i.e. April of any financial year. However, interest is not paid out if the tax refund is less than 10% of the total tax payable in a year.</p><p>The interest earned on tax refund is also taxable since it is considered under 'Ëœincome from other sources'.</p>
<h2>e-Filing TDS </h2>
<ul>
<li> In order to initiate the e-filing process, you would need to login or register yourself on the <a href="/tax/all-about-income-tax-department.html" target="_blank">Income Tax Department</a> website - https://incometaxindiaefiling.gov.in/.</li> 
<li>Once you have registered, you can choose to either download the relevant form and upload it back once you have completed or you can also fill the details online.</li> 
<li>The ITR-1 form is applicable for individuals earning a salary, pension, or income from property or other sources except for lottery.</li> 
<li>The ITR-2 form is applicable for individuals who are earning capital gains.</li> 
<li>The ITR-2 is applicable for a situation where there are no capital gains, however, the individual owns more than one house/property. For professionals and business owners, the ITR-3, ITR-4, and ITR-4S can be used.</li>
