<h2>What is Income Tax Refund?</h2>

<p>Being subject to income tax, we declared our projected investment amount at the beginning of each financial year. However, there are many times that the declared amount wouldnâ€™t match the actual investment amount due to various factors. It could be because of investments that started in the middle of a financial year or difference in income due to other factors. In instances where an individual has paid more tax than required for a financial year, the government provides an option to apply for a 
<a href="/tax/income-tax-refund.html" target="_blank">Income tax refund.</a></p>
<p><strong>How to apply for TDS Refund or Income Tax Return (ITR)?</strong></p>
<p>The process for income tax return is simple, all you need to do is, visit the website - https://incometaxindiaefiling.gov.in/, login or sign-up to download the relevant form for refund of income tax, enter all the particulars in the form, and submit it. The ITR forms are available for various categories, you would need to choose the form that is applicable for your income category. </p>
<p><strong>How does ITR work?</strong></p>
<p>If the scale of your income is not applicable for income tax and your employer had deducted a certain amount of money as tax, you can recover that amount by filing the ITR form. The ITR is also applicable for a situation where you have paid more than the required tax amount. Based on the details provided you, the Income Tax Department will calculate and review the taxable amount. If you are eligible for a refund, you will receive the excess amount in your bank account directly or as a cheque in your name.</p>
<p>You will be eligible for a 6% interest on your <a href="/tax/income-tax-return.html">ITR</a> amount if the refundable amount is more than 10% of the total taxable amount. </p>
<p><strong>The procedure for raising a complaint about delay in receiving ITR</strong></p>
<p>Even after applying for ITR, if you havenâ€™t received your refund or there is a delay in receiving the refundable amount, you can get in touch with your Income Tax Officer to file a dispute. You will need to contact your Income Tax Officer in writing with all the necessary details and documents. If you do not receive a satisfactory response or any response for that matter, you can contact the Income Tax Ombudsman with the following details â€“</p>
<ul>
<li>Your PAN number</li>
<li><a href="/tax/form-16.html" target="_blank">Form 16</a></li>
<li>Bank statement</li>
<li>TDS certificate issued by your bank, and</li>
<li>All the documents that show the investments and earnings</li>
