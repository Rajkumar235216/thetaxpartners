<h3 id="faqs">FAQs about TDS Refund</h3>
<ol>
   <li><strong>Will I get a TDS refund even if I have not provided my bank account details in the TDS Certificate or Income Tax Return?</strong></li>
<p>Your correct bank account details should be provided in your TDS Certificate or ITR to allow the Income Tax Department to transfer the refund amount through NEFT directly to your account. Any mistake in the bank account could lead to either transfer of the amount to someone else or delay and non-transfer. If you have not provided a bank account number, you must ensure that at least the address you have mentioned in the ITR/linked to PAN is correct, because the tax department might send you a cheque of the refund amount.</p>
<li><strong>How do I change my address so that I don't miss the refund cheque?</strong></li>
<p>You can log in to the <a href="/tax/step-by-step-guide-efile-income-tax-return-online.html">Income Tax E-Filing</a> portal and raise a Refund Re-issue Request. But this can be done only if you did not receive the refund cheque and it was returned to the Income Tax Department. If someone else has collected it in your name, then the taxman will no longer be responsible for the amount. The Refund Re-issue Request can be found in the 'My Account' option on the e-filing portal. You can choose your mode of refund receipt 'â€œ Electronic Clearance Service (ECS) or cheque. You can change both the address and bank account number through this option. This information will be updated for all income tax-related documents.</p>

<li><strong>Is there an option to change contact details from what I provided in the ITR?</strong></li>
<p>Yes. On the Income Tax e-filing portal, on the 'Profile Settings', you will find 'Update Contact Details'. You can change your address, email id, and mobile number through this option.</p>

<li><strong>What are the different kind of refund statuses that I may see in my e-filing portal profile?</strong></li>
<p>The Income Tax e-filing portal has 11 status options for your refund amount. They are:</p>
<ul>
<li><strong>No e-Filing has been done for this assessment year:</strong> If ITR was filed physically and not online, then the status will not be displayed in your profile on the website. This might also mean that haven't filed your ITR at all.</li>
<li><strong>Not determined:</strong> The I-T Department is yet to process your return and a refund amount has not been ascertained.</li>
<li><strong>Refund paid:</strong> The refund has been dispatched. If you haven't received it, check with your bank and your nearest post office. Also check your address and bank account number on the e-filing profile to confirm that there is no mistake. </li>
<li><strong>No demand no refund:</strong> The tax deduction was perfect and the I-T department owes you nothing. If you had claimed refund and this status shows up, it means the taxman's calculations were different from yours and the department does not think it owes you anything. If there are any errors in your ITR, you can either rectify or revise it online.</li>
<li><strong>ITR processed, refund determined and sent out to Refund Banker:</strong> The refund is being processed, so just wait a little more.</li>
<li><strong>Refund unpaid:</strong> Either your bank account number or your address is wrong and the department could not deliver the refund to you.</li>
<li><strong>Contact jurisdictional Assessing Officer:</strong> The department needs clarification or more information on your income tax return and you must get in touch with your jurisdictional AO for more details.</li>
<li><strong>Demand determined:</strong> Your refund request has been rejected and you have been found to owe the department more money as tax. If they are right, you need to follow the procedure suggested by the taxman and pay the dues before deadline. But if you made any mistake in the ITR, you can either rectify or revise it with the correct details.</li>
<li><strong>Rectification processed refund determined and sent out to Refund Banker:</strong> If you had filed an ITR rectification request it has been accepted and processed.</li>
<li><strong>Rectification processed demand determined:</strong> If you had filed a rectification, it has been accepted but the department still thinks you owe them some more money in taxes. You will need to pay the amount within 30 days of receiving a notice on this.</li>
<li><strong>Rectification processed no demand no refund:</strong> If you had filed a rectification, it has been accepted but the department estimates that there is no tax dues or refunds in your case. </li>
</ul>
</ol>



