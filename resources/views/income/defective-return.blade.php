        <h4>Notice for defective return u/s 139(9)</h4>
        <p>

            It often happens that while filing income tax returns we omit things or commit some mistakes. These
            mistake(s) make your return “defective” and you’re issued a notice of defective return u/s 139(9). Section
            139(9) of the Income Tax Act, 1961 states that when a return is found defective, the A.O. gives you a period
            of 15 days to correct the mistake. The return can be considered defective for one or many reasons as stated
            below. </p>
        </p>

        <h2>Common errors that can make your return “defective”?</h2>
        
        <ul>
            <li>Not filling the annexure, statements and columns in the income tax return that must be duly filled
                wherever required. For example, while claiming deduction u/s 80G, the details in its schedule is not filled or wrongly
                filled. </li>
            <li>Tax together with interest, if any is paid before filing the return and all the details relating to it
                is not filled. For example, BSR code, Date of challan should be correctly filled.</li>
            <li>Tax actually paid does not match with the tax payable in the income tax return or taxes are not paid in
                full.</li>
            <li>While filing ITR 4, total presumptive income is shown less than 8% or 6 %of gross turnover or receipts
                as the case may be then in that case ITR 3 should have been filed.</li>
            <li>The Gross receipts is not mentioned in the Profit &amp; Loss A/c Or the Gross receipt or income u/s 44AD is shown more than Rs. 2 Crore in ITR 4. </li>
            <li>If you have file your return u/s 44ADA with the gross receipt more than 50 Lakhs without Balance sheet
                and Profit &amp; Loss, then notice will be recieved for filing ITR-3 with B/s and P&amp;L Statement.
            </li>
            <li>Where you’re required to maintain regular books of account such as Balance Sheet and Profit and Loss
                statement, but they have not been filled in the return while filing it.</li>
            <li>Tax deducted has been claimed as a refund, but no income details are provided in the return.</li>
            <li>No income details has been provided in ITR but details regarding taxes paid have been provided.</li>
            <li>Gross income as referred in 26AS has not been considered in the respective heads of ITR.</li>
            <li>Name mismatch between PAN and Income Tax Return.</li>
        </ul>

            <h2>What shall I do after receiving notice u/s 139(9)? </h2>
  
            <p>Once you’ve been served with notice of  defective return u/s 139(9), then you must correct your return by revising it
                within 15 days from the receipt of notice by the Income Tax Department. You can also apply for extension
                by writing an application to the Assessing Officer (A.O.) requesting an extension of the deadline of
                filing a revised return. Practically, it is seen that even taxpayer rectifies the defect after the
                expiry of fifteen days but before the assessment is made, the Assessing Officer may condone the delay
                and treat the return as a valid return. </p>

            <h2>What will happen if I don’t do anything after receiving notice for defective return u/s 139(9)?</h2>
            
            <p>Your defective return will be treated as a non-filed or invalid return. This means the Income Tax
                Department will consider as if you’ve not filed a return for the year. </p>
            
            <h2>How to give a response to the Notice u/s 139(9)?</h2>
 
            <p> <b>Step 1:</b> Login to www.incometaxindiaefiling.gov.in with your user ID and Password.</p>
            <p><b>Step 2:</b> Click on tab “e-file” and in the drop-down select “e-File in Response to notice u/s
                139(9)”
            </p>
         
            <p> <b>Step 3:</b> On successful validation, you’ll find notice for defective return u/s 139(9) if any
                issued to you.</p>
  
            <p>The defective return notice u/s139(9) can be issued by</p>
            <ol>
                <li> Assessing Officer or</li>
                <li> CPC (Central Processing Center) </li>
            </ol>
            <p>The response submission in both these cases will differ as under</p>
            
            <p> <b>Step 4:</b><strong> In case, defective notice is raised by an Assessing Officer</strong> Click on
                submit and you’ll see the below image if defective notice is raised by Assessing Officer (A.O.):</p>
          
            <p><b>Step 5: </b>Now fill your Income Tax Return by rectifying the defect by selecting “Return u/s 139(9)
                and mention date of original return and acknowledgement number. Once you do that then generate its xml
                file and upload it in the above screen. After that you’ll see a message and your work is
                done.</p>
            
            <p><b>Step 6:</b> <strong> In case, defective notice is raised by CPC</strong> 
            <p><b>Step 7:</b>Now if you agree with the defect then just fill your Income Tax Return by rectifying the
                defect by selecting “Return u/s 139(9) and mention date of original return and acknowledgement number.
                Once you do that then generate its xml file and upload it in the screen.

            <p><b>Step 8:</b> But if you do not agree with the defect then select “No” and give “Assessee Remarks” and
                click submit. After that Income Tax Department, shall take follow up regarding it. </p>
           
            <strong>If selected the wrong ITR Form &amp; also want to make some changes in XML. Can I update the
                responses submitted?</strong>
            <p>You can update the response submitted by you by clicking on update link under Response to Notice u/s
                139(9) :</p>
            <p><b> Step 1 :</b> Click on tab “e-file” and in the drop-down select “Response to notice u/s 139(9)” </p>

            <p><b> Step 2 :</b> Click on update link</p>
  
            <p><b> Step 3 :</b> Select correct ITR Form &amp; attach XML and click on update </p>
  
            <h4><strong> Can I withdraw the response made to defective notice u/s 139(9)?</strong></h4>
            <p>Earlier it was possible to withdraw your response submitted to defective return notice under section
                139(9). But this functionality is no more available. Hence, now you cannot withdraw the response made,
                instead you can update or view the same. </p>
         
           
      
