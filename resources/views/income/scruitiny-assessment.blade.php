            <h4>Income Tax Scrutiny Assessment</h4>
            <p><strong>Scrutiny Assessment-Meaning</strong></p>
            <p>Scrutiny Assessment is an assessment of the income tax return by giving a chance to the assessee to
                substantiate the incomes, losses, expenses, deductions, etc which are claimed in the tax return with
                proof.</p>
            <p><strong>How is the Scrutiny Assessment conducted?</strong></p>
            <p>While scrutinizing, the assessing officer enquires the taxpayer. The scrutiny is done in order to ensure
                that the details given by the taxpayer are valid and correct. If any discrepancies are found, the
                assessing officer may charge penalties from the assessee for the same.</p>
            <p>The scrutiny assessment is conducted in order to ensure that the assessee has not understated the income,
                calculated excessive loss or underpaid tax in any manner.</p>
            <p><strong>There are two types of scrutiny assessment:</strong></p>
            <ol>
                <li>Manual scrutiny assessment</li>
                <li>Compulsory scrutiny assessment</li>
            </ol>
            <p><strong>Manual Scrutiny Assessment&nbsp; </strong></p>
            <ul>
                <li><strong>Not filing Income Tax Return</strong></li>
            </ul>
            <p>Each person is required to<strong>file ITR online</strong> on
                time by ensuring that the all the details match correctly. If the income of persons is more than the
                Basic Tax Exemption Limit, they are required to file the tax returns. Tax Return has to be filed even if
                the tax has already been deducted. If a person does not pay his/her taxes on time, he/she will get a
                notice from the Income Tax Department.</p>
            <ul>
                <li><strong>Declaring less Income/More Loss as compared to previous years</strong></li>
            </ul>
            <p>If a person, knowingly or unknowingly shows less income or more loss which is less as compared to
                previous years, then the IT department may get suspicious about it. In such cases, the department can
                think of scrutinizing the person’s income.</p>
            <ul>
                <li><strong>Difference in TDS tax credit Claim and 26AS</strong></li>
            </ul>
            <p>26AS is the tax credit statement and gives the details of TDS deposited on the behalf of a person. One
                should check the details of all TDS payments and match the details correctly since the TDS details can
                be viewed from the IT department’s website or from bank’s online portal. There should not be any
                difference in the amount of TDS that is being claimed in the Income Tax Return and the TDS that is
                actually viewed in the form 26AS. If there is a mismatch in the amount, then the person may not get the
                refund claimed.</p>
            <ul>
                <li><strong>Non-Declaration of Exempted Tax</strong></li>
            </ul>
            <p>There are some incomes that are exempted from tax payment but one should not hide it from the Income Tax
                Department. Even though they are exempt, they should be shown on the income tax return because the tax
                department needs to verify all the income of the person.</p>
            <ul>
                <li><strong>Interest from Fixed Deposits or Savings Account</strong></li>
            </ul>
            <p>Banks usually deduct 10% of the deposits’ interest by default, but one is supposed to pay any additional
                tax which is applicable, depending on the tax bracket in which the person falls.</p>
            <ul>
                <li><strong>Claiming large refunds in return of income</strong></li>
            </ul>
            <p>One can get scrutinized for claiming higher tax refunds. The Income Tax Department may want to know the
                reasons for claiming the higher tax refunds. People usually file Forms 15G/15H in order to escape the
                TDS but one should be careful enough to not that the Income Tax Department gets all the details from the
                banks. If anyone is caught by the department stating mismatch, the person may be scrutinized.</p>
            <ul>
                <li><strong>Availing double benefits due to change in Job</strong></li>
            </ul>
            <p>It happens sometimes that a person who has changed job recently gets Form 16 and fails to declare the
                employer and pay the due taxes. This may be due to the benefits are given twice. People often forget to
                calculate their earlier tax liability; this may lead to lower deduction of taxes. Thus, one must be
                careful and should not forget to calculate taxes on the previous income after changing the job to avoid
                later implications due to less tax payments.</p>
            <ul>
                <li><strong>High-Value Transactions</strong></li>
            </ul>
            <p>The Income Tax Department gets all the information of the high-value transactions and this can increase
                the chances of being scrutinized by the Income Tax Department. Hence, it is imperative to involve in
                less big transactions and avoid problems later on.</p>


            <p><strong>Compulsory Scrutiny Assessment</strong></p>
            <p>The following cases are compulsorily taken for scrutiny and are unavoidable, however, some prevention can
                be taken avoid scrutiny by being careful and following the rules. The following cases state when the
                scrutiny is compulsory:</p>
            <ul>
                <li>If the addition in earlier assessment year is more than Rs. 10 Lacs on a substantial and repeating
                    question of law or fact which is confirmed in appeal or is pending before an appellate authority
                </li>
                <li>If the addition in earlier assessment year on transfer pricing in excess of Rs. 10 crores or more on
                    a substantial and repeating question of law or fact which is confirmed in appeal or is pending
                    before an appellate authority</li>
                <li>Assessments relating to Survey under Section 133A of the <strong><a
                            href="http://www.incometaxindia.gov.in/pages/acts/income-tax-act.aspx">Income Tax
                            Act</a></strong> excluding the cases where there are no impounded books of accounts and the
                    income returned is not less than the income return of the previous assessment year. The scrutiny can
                    be done in the cases where there is concealment of income which may be based on survey report or any
                    other report.</li>
                <li>Assessments of search and seizures are to be done under the Sections 158B, 158BC, 158BD, 153A, and
                    153C. The Assessment for the returns filed for the assessment year related to the previous year in
                    which the search was conducted is done under Sections 132/132A of the Income Tax Act.</li>
                <li>Returns filed in response to Section 148 of the Income Tax Act. If any income assessment has been
                    missed out, the cases u/s 148 can be reopened. Any case can be reopened within six months from the
                    end of the assessment year.</li>
                <li>If the cases where the registration u/s 12AA of the Income Tax Act has been canceled by the
                    concerned CIT/DIT, yet the assessee is claiming tax exemption under Section 11 of the Income Tax
                    Act. But the cases where the order of the CIT/DIT has been reversed in appellate proceedings are not
                    selected for scrutiny.</li>
                <li>Cases where the order denying the approval u/s 10 (23C) of the Income Tax Act or withdrawing the
                    approval granted which has been passed by the Competent Authority yet the assessee is found to have
                    claimed the tax exemption under the Act.</li>
                <li>If the specific and verified information regarding tax evasion is released by the department’s
                    authorities.</li>
                <li>Computer Aided Scrutiny Selection (CASS): CASS helps in selection of the cases for scrutiny. Such
                    cases are separately intimidated in due course by DGIT jurisdictional authorities concerned.</li>
            </ul>


