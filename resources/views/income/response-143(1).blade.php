                <h2 id="What-is-Intimation-us-143-1">What is Intimation u/s 143(1)? </h2>
                <div class="clearfix"></div>
            </div>
            <div class="col-md-12">
                <p> Intimation u/s 143(1) is a summary of the details which you have submitted to the tax department and
                    the details which department has considered while processing your return. Basically, the intimation
                    u/s 143(1) contains the following information:</p>
                <ul>
                    <li> (a) Permanent Details of assessee like name, address etc</li>
                    <li> (b) Income Tax Return filing details like acknowledgement number, filing date etc </li>
                    <li> (c) Communication reference number</li>
                    <li> (d) Refund sequence number </li>
                    <li> (c) Tax Calculation as provided by you in the Return of Income</li>
                    <li> (d) Tax as Computed under section 143(1) {i.e. As per Department}</li>
                </ul>

                <h2 >Why is the intimation u/s 143(1) issued?</h2>
                <div></div>
                <p>Basically, when a return is submitted to Income Tax Department, the department applies the following
                    computerized checks as a part of its review procedure:</p>
                <p>Arithmetical errors in the return.</p>
                <ul>
                    <li>An incorrect claim, which is apparent from any information in the return. For example, if the
                        deduction u/s 80C is claimed more than the maximum permissible deduction u/s section 80C i.e. Rs 1,50,000, the excess shall be disallowed and it will be
                        reflected in your intimation u/s 143(1). Another example may be that rent income deducted from
                        business income but same is not shown under Income from House Property.</li>
                    <li>Disallowance of expenditure indicated in the audit report but not taken into account in
                        computing the total income in the return</li>
                    <li>Comparison of Advance Tax, Self-assessment tax and TDS etc. from 26AS.</li>
                    <li> Addition of income appearing in Form 26AS or Form 16A or Form 16 which is not included in ITR (
                        it is applicable for returns filed upto AY 17-18)</li>
                    <li>Claiming the losses for carry forward to next year when return is submitted after the due date /
                        set off of losses of previous year where return was filed after the due date.</li>
                    <li>Whether deduction under section 10AA, 80-IA, 80-IAB, 80-IB, 80-IC, 80-ID, 80-IE has been taken
                        after the due date of Income Tax Return</li>
                    <li>Calculation of Tax, Late filing fees and Interest etc.</li>
                </ul>

                <strong>After these checks, have been applied, then Income Tax Department Issues Intimation u/s 143(1)
                    in the cases where there’s:</strong>
                <div></div>
                <ul>
                    <li>Increase/decrease in Tax/Interest Payable, or</li>
                    <li>Increase/decrease in Refund, or</li>
                    <li>Adjustment that makes a change in the loss claimed </li>
                </ul>
                <p>However, practically you can still receive it, even if your case does not fall in the above mentioned
                    three criteria. It is seen practically that Intimation u/s 143(1) is sent to all taxpayers by the
                    Income Tax Department. </p>
   
                <h2>Till when can I receive intimation u/s 143(1)?</h2>
                <div class="clearfix"></div>
                <p>Intimation u/s 143(1) can be issued only upto 1 year from the end of the financial year in which the
                    return is filed, and not after that.</p>
                <strong>For example,</strong>
                <p>
                    1. If you have filed your return for the year 2018-19 on 21.07.2019, then in that case, the
                    financial year-end will be on 31st March 2020 and intimation can be issued till 1 year i.e.
                    intimation u/s 143(1) can be issued for the FY 2018-19 only up to 31st March 2021.<br>
                    2. Lastly, do not ignore this notice as you’re required to submit your response within 30 days of
                    receipt of such notice, in case any action is required.</p>
                <hr class="hr">
                <h2 id="What-should-be-done-on-receiving-intimation-us-143-1">What should be done on receiving
                    intimation u/s 143(1)?</h2>
                <div class="clearfix"></div>
                <p>On receiving the intimation u/s 143(1), there’ll be two cases, either you’ll agree with the
                    computation done by the department or you’ll not.</p>

                <ul>
                    <li>In case you agree with the tax amount payable or amount of refund due You would be required to
                        pay the amount of outstanding taxes or you will simply receive the amount of refund shown in the
                        intimation u/s 143(1).</li>
                    <li>In case you do not agree with the calculations done by income tax department You can opt to file
                        an online request for  rectification of your income-tax return under section
                            154 or file an appeal under section 246A.<br>And, if the net amount refundable or
                        payable is zero, then you can treat the intimation received u/s 143(1) as the completion of the
                        return filing process from the Income Tax Department for the financial year in the relation to
                        which the return was filed.
                    </li>
                </ul>
                <hr class="hr">
                <h2 id="What-if-no-intimation-is-received-till-the-expiry-of-one-year">What if no intimation is received
                    till the expiry of one year?</h2>
                <div class="clearfix"></div>
                <p>In case, you do not receive any intimation till the expiry of one year from the end of the financial
                    year in which you have filed your return, then your  ITR–V acknowledgement
                    will be deemed to be your intimation in that case. However, it is still suggested that you check
                    online whether ITR has been processed or not by the Income Tax Department.</p>


                <h2 >How is the intimation u/s 143(1) received?</h2>
                <div class="clearfix"></div>
                <p> These intimations are auto-generated which are sent to the Email address provided by you at the time
                    of filing income tax returns online / mail id given at the time of registration on income tax
                    website. The sender of these mails is Central Processing Centre (CPC) with the sender id being
                    intimations@cpc.gov.in as the returns are processed at CPC only.
                </p>
                <p>As technology is growing, so is the Income Tax Department. Now with the sending of Intimation u/s
                    143(1), a text message is also sent on the registered mobile number.
 
                <h2 id="What-is-the-password-to-open-intimation-us-143-1">What is the password to open intimation u/s
                    143(1)?</h2>
                <div class="clearfix"></div>
                <p>The attachment received is a password protected file which is your PAN number in lowercase followed
                    by your date of birth in DDMMYYYY format. </p>
                <strong>For example, </strong>
                <p>Suppose your PAN is AAGRK5803P and your birth date is 2nd November, 1982, then the password to open
                    your online intimation u/s 143(1) shall be “aagrk5803p02111982”.
                </p>
                <hr class="hr">
                <div class="clearfix"></div>
                <h2 id="How-to-contact-the-Income-Tax-Department">How to contact the Income Tax Department?</h2>
                <hr>
                <div class="clearfix"></div>
                <p>You can contact the Income Tax Helpline/Toll-Free Number of CPC Bangalore at 1800 103 4455 or
                    91-80-46605200 for Income tax queries. You can easily contact them with the help of the Document
                    Identification Number mentioned on the intimation issued to you.</p>
 
                <h2 id="How-to-get-the-Intimation-us143-1-again">How to get the Intimation u/s143(1) again?</h2>
                <div class="clearfix"></div>
                <p>If you have not received your intimation on the registered mail Id or in case you are unable to find
                    that mail again you can get your intimation u/s 143(1) again by following any of these methods:</p>
                <ul>
                    <li> By downloading it from Income Tax India Official website or</li>
                    <li> Making a request for Reissue of intimation u/s 143(1)</li>
                </ul>
                <p>Let us understand both the processes one by one.</p>
                <b>By downloading it from Income Tax India Official website</b>
                <p>This facility has been newly introduced by the income tax department under which you can download the
                    intimation for the latest years by making a successful login to your income tax India efiling
                    account. To download the intimation </p>
                <ul>
                   <li>Make login to income tax India efiling 
                       
                    </li>
                    <li>Click on View Returns and Forms
                        
                    </li>
                    <li>Select income tax returns
                      
                    </li>
                    <li>Click on the acknowledgment number for which you want to get the information and for which the
                        ITR has been processed
                        
                    </li>
                    <li>Click on intimation and the intimation in pdf attachment will download.
                     
                    </li>
                </ul>
                <hr class="hr">
                <h4><b>Making a request for Reissue of intimation u/s 143(1)</b></h4>
                <p>All the intimations and communications by the income tax department are served by Email and many of
                    the time we miss checking our mailbox or accidently loose them. So, to get the copy of intimation
                    again you can make an online request for reissue of the intimation u/s 143(1)/154.
                <p>To sum up, Intimation u/s 143(1) is a computer-generated notice which contains the final amount of
                    tax payable or refunds to be granted, along with interest.</p>
                <hr class="hr">
                <h2 id="Check-the-following-points-when-you-receive-an-intimation-us-143-1">Check the following points
                    when you receive an intimation u/s 143(1)</h2>
                <div class="clearfix"></div>
                <ul>
                    <li> The intimation has your name on it.</li>
                    <li> The Intimation has document identification number </li>
                    <li> All Incomes are considered properly under the appropriate head and Income of one head is not
                        considered under another head or repeated elsewhere.</li>
                    <li> The deductions you have claimed under 80C and other sections of chapter VI A are considered.
                    </li>
                    <li> TDS/TCS claimed, Advance Tax paid and Self-Assessment Tax paid in the computation by CPC.</li>
                    <li> Any relief u/s 89, 90/90A/91 or any rebate claimed/ allowable is considered in the Intimation.
                    </li>
                </ul>
                <p>So, don’t get paranoid even if an intimation u/s 143(1) has been issued to you. Simply, go through
                    the intimation, gather your supporting evidence and respond!
