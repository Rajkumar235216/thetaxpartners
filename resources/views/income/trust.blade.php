                      <h3><em>(1) Cancellation of Registration of a Trust or Institution under Section 12AA(3)</em></h3>
                      <p>As per section 12AA, the registration once granted to a trust or institution shall remain in force till it is cancelled by the Principal Commissioner or Commissioner. </p>
                      <p>Consequently, the Principal Commissioner or Commissioner can after giving reasonable opportunity of being heard to the concerned trust or institution, pass an order under section 12AA(3) in writing cancelling the registration under the following two circumstances:</p>
                      <ol type="a">
                        <li>
                          <p> the activities of a trust or institution are not genuine, or; </p>
                          </li>
                        <li>
                          <p> the activities are not being carried out in accordance with the objects of the trust or institution. </p>
                          </li>
                        </ol>
                      <p>Only if either or both the above conditions are met, would the Principal Commissioner/ Commissioner be empowered to cancel the registration, and not otherwise. </p>
                      <p>However, no order under section 12AA(3) shall be passed unless such trust or institution has been given a reasonable opportunity of being heard </p>
                      <h3><em>(2) Cancellation of Registration of a Trust or Institution under Section 12AA(4)</em></h3>
                      <p>The powers of Principal Commissioner/Commissioner to cancel registration under section 12AA(3) are severely restricted. There have been cases where trusts, particularly in the year in which they have substantial income claimed to be exempt under other provisions of the Income Tax Act, deliberately violate provisions of section 13 by investing in prohibited mode etc. Similarly, there have been cases where the income is not properly applied for charitable purposes or has been diverted for benefit of certain interested persons. Due to restrictive interpretation of the powers of the Principal Commissioner/ Commissioner under section 12AA, registration of such trusts or institutions continues to be in force and these institutions continue to enjoy the beneficial regime of exemption. </p>
                      <p>Whereas under section 10(23C), which also allows similar benefits of exemption to a fund, Institution, University etc, the power of withdrawal of approval is vested with the prescribed authority if such authority is satisfied that such entity has not applied income or made investment in accordance with provisions of section 10(23C) or the activities of such entity are not genuine or are not being carried out in accordance with all or any of the conditions subject to which it was approved. </p>
                      <p>Therefore, in order to rationalise the provisions relating to cancellation of registration of a trust, section 12AA(4) was inserted to provide that where a trust or an institution has been granted registration, and subsequently it is noticed that section 13(1) is applicable as its activities are being carried out in such a manner that,â€” </p>
                      <ol type="i">
                        <li>
                          <p> its income does not enure for the benefit of general public; </p>
                          </li>
                        <li>
                          <p> it is for benefit of any particular religious community or caste (in case it is established after commencement of the Income-tax Act); </p>
                          </li>
                        <li>
                          <p> any income or property of the trust is applied for benefit of specified persons like author of trust, trustees, etc.; or </p>
                          </li>
                        <li>
                          <p> its funds are invested in prohibited modes, </p>
                          </li>
                        </ol>
                      <p>then the Principal Commissioner or the Commissioner may by an order in writing cancel the registration of such trust or institution. </p>
                      <p>However, registration shall not be cancelled under section 12AA(4) if such trust or institution proves that there was a reasonable cause for the activities to be carried out in the above manner.</p></td>
