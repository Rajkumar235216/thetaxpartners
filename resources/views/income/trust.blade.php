<div class="fsize16"><p style="text-align: justify;">Section 12A registration is one time exemption obtained by most Trusts, right after incorporation to be exempted from paying income tax. Trusts and NGOs having 12A registration enjoy exemption from paying income tax on the surplus income of the Trust or NGO. Income tax exemption is available for all <a href="https://taxguru.in/income-tax/taxation-of-non-profit-organisations-chapter-vi-under-the-revised-direct-tax-code.html" target="_blank" rel="noopener noreferrer"><strong>non-profit NGOs</strong></a>. Hence, it is important for all Trusts, NGOs and other Not-for-Profit organizations to be aware of Section 12A of Income Tax Act and obtain the same, soon after incorporation of the Trust or NGO.</p>
<p style="text-align: justify;"><strong>Benefits of 12A Registration</strong></p>
<p style="text-align: justify;">The benefits of Section 12A Registration are as follows.</p>
<ul style="text-align: justify;">
<li>The fund that is used for the charitable or religious purposes is considered to be the income application. The income application is nothing but the expenses used for charitable or religious purposes when calculating the income of the trust.</li>
<li>The income received will be free from taxation.</li>
<li>The person registered under this section can avail benefits for accumulating or setting aside income which is not more than&nbsp;15% for the charitable or religious purposes.</li>
<li>The accumulation of income, that is considered to be the income application according to Section 11(2) will not be included in the total income.</li>
<li>The registration that is made under Section 12A, is a one-time registration. Once the registration is made, it will be active till the date of cancellation of the registration. There is no requirement of renewing the registration. Therefore, these benefits can be claimed whenever required by the NGO.</li>
</ul>
<p style="text-align: justify;"><strong>Eligibility for 12A Registration</strong></p>
<p style="text-align: justify;">Only Charitable Trusts, Religious Trusts, Societies and companies that come under Section 8 are eligible for 12A registration. 12A registration is not applicable for Private or Family Trust.</p>
<p style="text-align: justify;"><strong>Form 10A â€“ Income Tax</strong></p>
<p style="text-align: justify;">Charitable Trusts, Religious Trusts, Societies and Section 8 Companies claiming exemption under Section 11 and 12 of the Income Tax Act, must obtain 12A registration. Private or family trusts are not allowed such exemptions and cannot obtain 12A registration. The process for applying for 12A registration and Form 10A filing has been made online only with the&nbsp;digital signature&nbsp;of the signatory.&nbsp;To obtain 12A Registration, an application in&nbsp;Form 10A&nbsp;for registration of charitable or religious trust or institution must be made online to the Commissioner of Income Tax along with the following documents.</p>
<p style="padding-left: 40px; text-align: justify;">1. Where the trust is created, or the institution is established, under an instrument, self-certified copy of the instrument creating the trust or establishing the institution;</p>
<p style="padding-left: 40px; text-align: justify;">2. Where the trust is created, or the institution is established, otherwise than under an instrument, self-certified copy of the document evidencing the creation of the trust, or establishment of the institution;</p>
<p style="padding-left: 40px; text-align: justify;">3. Self-certified copy of registration with Registrar of Companies or Registrar of Firms and Societies or Registrar of Public Trusts, as the case may be;</p>
<p style="padding-left: 40px; text-align: justify;">4. Self-certified copy of the documents evidencing adoption or modification of the objects, if any;</p>
<p style="padding-left: 40px; text-align: justify;">5. Where the trust or institution has been in existence during any year or years prior to the financial year in which the application for registration is made, self-certified copies of the annual accounts of the trust or institution relating to such prior year or years (not being more than three years immediately preceding the year in which the said application is made) for which such accounts have been made up;</p>
<p style="padding-left: 40px; text-align: justify;">6. Note on the activities of the trust or institution;</p>
<p style="padding-left: 40px; text-align: justify;">7. Self-certified copy of existing order granting registration under section 12A or section 12AA, as the case may be; and</p>
<p style="padding-left: 40px; text-align: justify;">8. Self-certified copy of order of rejection of application for grant of registration under section 12A or <a href="https://taxguru.in/income-tax/section-12aa-application-cannot-rejected-without-affording-opportunity-heard-assessee.html" target="_blank" rel="noopener noreferrer"><strong>section 12AA</strong></a>,as the case may be, if any.</p>
<p style="text-align: justify;"><strong>Documents Required for 12A Registration</strong></p>
<p style="text-align: justify;">The following documents must be submitted by the 12A registration applicant in Form 10A:</p>
<ul style="text-align: justify;">
<li>Certified copy of the instrument under which the trust/institution was created/established, if applicable</li>
<li>Certified copy of the document evidencing the creation of the trust or the establishment of the institution, if applicable</li>
<li>Certified copy of registration with RoC/Registrar of Firms &amp; Societies/Registrar of Public Trusts, whichever applicable</li>
<li>Certified copy of the documents evidencing adoption or modification of the objects, if any</li>
<li>Certified copy of the annual reports of the trust/institution for a maximum three immediately preceding financial years, if applicable</li>
<li>Note on activities</li>
<li>Certified copy of existing order granting registration under section 12A or section 12AA, if any.</li>
<li>Certified copy of order of rejection of application for grant of registration under section 12A or section 12AA, if any.</li>
</ul>
<p style="text-align: justify;"><strong>Procedure for Obtaining&nbsp;12A Registration</strong></p>
<p style="text-align: justify;">Once, the application is submitted in the required format online, the Commissioner will request the applicant to submit additional information or documents, as the Commissioner sees fit. The request for additional information or documents would be to satisfy the Commissioner as to the genuineness of the activities of the Trust or Institution.</p>
<p style="text-align: justify;">If the Commissioner is satisfied with the application, then he/she will pass an order in writing registering the Trust or Institution under 12A of the Income Tax Act. In case the Commissioner is not satisfied with the application, then he/she can also pass an order in writing refusing to register the Trust or Institution.</p>
<p style="text-align: justify;">As per the provisions of Section 12AA(2), every order granting or refusing registration, shall be passed by the registering authority before the expiry of six months from the end of the month in which the application was received.</p>
<p style="text-align: justify;">On average, 12A registration take 1 to 3 months in India. However, once a Trust obtains registration, its is valid for the lifetime of the Trust and there is no requirement for renewal.</p>
