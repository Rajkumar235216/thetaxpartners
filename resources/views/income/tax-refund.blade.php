        <h4>What is Income Tax Refund</h4>
        <p>Income Tax refund arises in case of a mismatch between the tax amount paid and the actual payable amount. If
            the amount paid is higher than the actual amount payable, a refund is initiated. The Form 30 is used for the
            same purpose.</p>
        <p>Under the income tax and other <a href="/tax/direct-tax.html">Direct Tax</a> laws, refunds arise in those
            cases where the amount of tax paid by a person (or paid on his/her behalf) is greater than the amount on
            which he/she is properly chargeable. This is noted under Sections 237 to 245 of the Income Tax Act, 1961.
        </p>
        <h4 id="how-to-get-it-refund">How can I get IT refund in India?</h4>
        <p>When you file the return of your income, you can avail tax refund. In general, the date for filing <a
                href="/tax/income-tax-return.html">income tax returns</a> is July 31 of every year unless extended.</p>
        <h4>What is the Income Tax amount that I will get back?</h4>
        <p>In order to find the amount of <a href="/income-tax.html">income tax</a> that you will get back as the
            refund, you must calculate the tax liability that is associated with you. If the amount that you have paid
            as taxes is more than the tax liability, then you will get the extra amount as a refund.</p>

        <h4 id="how-can-i-claim-it-refund">How can I claim my Income Tax refund?</h4>
        <p>The easiest way to file for your tax refund is to declare your investments in <a
                href="/tax/form-16.html">Form 16</a> (life insurance premiums paid, house rent being paid, investments
            in equity/NSC/mutual funds, bank FDs, tuition fees, etc.) while filing your IT return and submit the
            necessary proofs. If you’ve failed to do so and have been paying extra taxes you think you could have
            avoided, you will need to fill out Form 30.</p>
        <p>Form 30 is basically a request for your case to be looked into and the excess tax that you have paid is
            refunded. Your income tax refund claim needs to be submitted before the end of the financial year. Your
            claim needs to be accompanied by a return in the form (prescribed under section 139).</p>
        <h5>The Form 30 format is as follows: (Sample)</h5>
        <p>I, (your name), of (address), do hereby state that my total income computed in accordance with the provisions
            of the Income Tax Act, 1961, during the year ending on (year) being the previous year for the assessment
            year commencing on the 1st of April (year), amounted to Rs. (amount); that the total income tax chargeable
            in respect of such total income is Rs. (amount) and that the total amount of Income Tax paid or treated as
            paid under:</p>
        <p>Section 199, is Rs. (amount).</p>
        <p>I, therefore, request for a refund of Rs. (amount).</p>
        <p>(Signature)</p>
        <p>I hereby declare that I was resident/resident but not ordinarily resident/non-resident during the previous
            year relevant to the assessment year to which this claim relates and that what is stated in this application
            is correct.</p>
        <p>Dated: (date)</p>
        <p>(Signature)</p>
        <strong>It is important to note that:</strong>
        <ul>
            <li>This claim should contain a document of proof of return of income in a prescribed form, unless you’ve
                already made such a claim to the Assessing Officer.</li>
            <li>Non-residents whose income is subject to <a href="/tax/all-about-tds.html">TDS</a> should make the claim
                for refund to the “Assessing Officer, Non-resident Refund Circle, Bombay”.</li>
        </ul>
        <p>If you have been charged tax under the provisions of Section 192 – 194, Section 194A and Section 195 on your
            income (for dividends, etc.), the claim should be accompanied by the necessary certificates recommended
            under Section 203.</p>
        <h4 id="how-is-it-refund-processed">How is Income Tax refund processed?</h4>
        <p>The Income Tax authorities who are present at the Centralised Processing Centre (CPC) in Bengaluru are
            responsible for processing the Income Tax (IT) refund. The refunds are processed once the assessee files
            his/her Income Tax Returns (ITR). If any tax reimbursement arises at the time of processing the ITRs, the IT
            refund banker receives the orders for refund of income tax which are generated and transferred by the IT
            authorities.</p>

        <h4 id="how-is-payment">How is the payment of income tax refund made?</h4>
        <p>The payment of income tax refund is made in one of the following methods:</p>

        <ul>
            <li><strong>Direct transfer of the amount to be refunded to the taxpayer’s bank account:</strong> This is
                the usual method which is used for the purpose of transferring the income tax refund amount to the
                taxpayers. The transaction might be made through NECS/RTGS. It is important on the part of the taxpayer
                to make sure that all the details pertaining to his/her bank account are properly furnished in the
                return forms at the time of filing the returns. This enables the easy and fast movement of funds
                directly to the account.</li>
            <li><strong>Income Tax Refund via cheque:</strong> This method is an alternative option for transferring the
                Income Tax refunds. In the case of the bank details provided by the taxpayer at the time of filing the
                Income Tax Returns (ITRs) is not unclear, incomplete, or wrong, the Income Tax authorities issue a
                cheque addressing the account number which is furnished by the taxpayer at the time of filing his/her
                returns.</li>
        </ul>


        <h4 id="how-to-track">How do I track my Income Tax Refund?</h4>
        <p>The IT department allows you to track the status of your refund. If your refund procedure has not been
            completed by your officer in charge, you will receive a message notifying you of the same.</p>
        <p>Just follow these two steps to claim income tax refund.</p>
        <ol>
            <li><strong>Get Refund through Direct Transfer:</strong> Excess tax paid can be refunded to you by crediting
                your bank account with ECS transfer. RTGS/NECS are also used to transfer the tax refund directly into
                your account, using your 10-digit account number and MICR code, through the State Bank of India. You can
                track your income tax refund through the http://www.incometaxindia.gov.in website or through NSDL-TIN
                website by clicking on “Status of Tax Refunds”. You will then need to enter your PAN number and
                assessment year for refund details.</li>
            <li><strong>Refund by cheque:</strong> You can track this with the speed post service that has been tasked
                with delivering it, using the reference number that the IT department will give you.</li>
        </ol>


        <h4 id="due-date-to-claim">Due Date&nbsp;to Claim Income Tax Refund&nbsp;</h4>

        <p>Income tax refunds must be claimed within one year from the date on which the assessment year ends. However,
            in certain cases, assessing officers tend to entertain refund claims that were filed after the specified due
            date. Here are some points you need to keep in mind:&nbsp;</p>

        <ul>
            <li>Income tax refund claims will not be considered if six successive assessment years have been
                completed.&nbsp;</li>
            <li>The refund amount must be less than Rs.50 lakh for a single assessment year.&nbsp;</li>
            <li>Interest will not be offered on refund of late claims.&nbsp;&nbsp;</li>

            <li>If the delayed claims require verification, the assessing officer could reconsider the
                claim.&nbsp;&nbsp;</li>
        </ul>

        <h4>Interest on Delayed Income Tax Refund&nbsp;</h4>


        <p>Under Section 244A of the <a href="/tax/income-tax-act.html">Income Tax Act</a>, in case the refund payment
            is delayed, the Income Tax Department is liable to pay interest at 6%. The interest applicable to your
            refund amount shall be computed from the date on which the tax was paid to the date on which the refund was
            made. For example, if you claim a refund of Rs.10,000&nbsp;for AY 2017-18 and you received the refund in
            March 2018, the interest applicable to your refund will be computed from April 2017 to March 2018.&nbsp;</p>
        <h4>Am I eligible for an Income Tax refund?</h4>
        <p>There are many cases wherein you will be eligible for a refund. Some of them are:</p>

        <ul>
            <li>If the tax you’ve paid in advance on the basis of self-assessment is more than the tax payable on the
                basis of regular assessment.</li>
            <li>If your tax deducted at source (TDS) from salary, interest on securities or debentures, dividends, etc.
                is higher than the tax payable on the basis of regular assessment.</li>
            <li>If the tax charged, based on regular assessments, gets reduced because an error in the assessment
                process was resolved.</li>
            <li>The same income is taxed in a foreign country (with which the government of India has an agreement to
                avoid double-taxation) and in India as well.</li>
            <li>If you have investments which offer tax benefits and deductions that you have not declared.</li>
            <li>If you find, after considering the taxes you’ve paid and the deductions you are allowed, that the tax
                paid amount is in the negative.</li>
        </ul>

        <h2>FAQs</h2>

                    <p>When am I eligible for income tax refund?</p>

                    <p>When the amount of income tax paid is more than the income tax you are liable to pay, then as a
                        taxpayer, you are eligible for an income tax refund.</p>
                
                    <p>How do I file my income tax refund?</p>
          
                    <p>The easiest and fastest way to file your tax refund is to declare your investments in Form 16. In
                        order to file your tax refund, you can visit www.incometaxindiaefiling.gov.in and file your
                        income tax refund.</p>
              
                    <p>How do I track my income tax refund?</p>
            
                    <p>There are 2 ways to track your income tax refund. One way is to use the reference number that the
                        IT department will give you, if you’ve claimed it through cheque. If you have claimed it via
                        direct transfer, then you can track the income tax refund via NSDL-TIN website by clicking on
                        the ‘Status of Tax Refunds’ option or through the Income Tax Departmental website.</p>
   
                    <p>What are the ways in which tax refund is generated?</p>
           
                    <p>Generating income tax refund is an extremely simple procedure. The payment is either done by
                        cheque or is directly credited to your registered bank account.</p>
      
                    <p>How can I check my income tax refund status?</p>
             
                    <p>You can visit the official website of the income tax department to track the refund status.</p>
          
                    <p>How can I claim my income tax refund after due date?</p>
             
                    <p>If you want to claim your income tax return after the due date of 31st July of every year, you
                        can do so by filing for a belated return. A belated return can be filed either before the
                        completion of the assessment year or by the end of the relevant assessment year, whichever is
                        earlier. For instance, for the assessment year 2019 - 20, you can file a belated return before
                        31st August 2019 or, at the most, by 31st March 2020.</p>
      
                    <p>How can I get income tax refund?</p>
              
                    <p>The Income Tax Department offers income tax refund via two methods – cheque and account transfer
                        through ECS. You can opt for either of these options, as per your preference.</p>
             
                    <p>How long does it take to get income tax refund?</p>
          
                    <p>For salaried persons, income tax refund may be credited to their registered accounts within a
                        short period of two weeks. But this is true only for those income tax returns that have been
                        filed at the earliest. On an average, the estimated time span for income tax refund is between 1
                        month and 4 months.</p>
                    <p>For business organizations, income tax refund or IT refund is generally credited between 3 months
                        to 6 months.</p>
                
                    <p>How long does it take to get your tax refund direct deposit?</p>
              
                    <p>In case of direct deposit to your registered bank account, income tax refund is processed within
                        2 months to 6 months from the date of e-filing your income tax return online.</p>
        
                    <p>How many days does it take for income tax refund?</p>
             
                    <p>In case of direct deposit to your registered bank account, income tax refund is processed within
                        1 months to 4 months from the date of e-filing your income tax return online. In case of income
                        tax returns that are filed physically, it takes longer for them to receive their income tax
                        refund.</p>
              
                    <p>When can I get my tax refund for 2018?</p>
            
                    <p>You will receive the income tax refund for the financial year 2018-19 in the year 2019. It
                        generally takes a taxpayer a period of 1 month to 4 months from the date of filing for income
                        tax returns, to receive the same.</p>
               
                    <p>My Income Tax refund is still not processed. How do I escalate this?</p>
              
                    <p>In case you have not received your refund even after 3 or 4 months after the deadline of 31st
                        July of the assessment year, it is recommended that you get in touch with the Centralized
                        Processing Centre (CPC) of the Income Tax Department on 1800 4250 0025 or 080-2650 0025 to
                        inquire the reason(s) behind the same.</p>
            
                    <p>How to check Income Tax refund status from government’s website?</p>
            
                    <p>You can check your income tax refund status by following the few simple steps:</p>
                    <ul>
                        <li>
                            <p>Visit the government’s official e-Filing website and select the ‘ITR Status’ button or
                                click here to be redirected to the relevant page.</p>
                        </li>
                        <li>
                            <p>Login to the website with your User ID, Password and Date of Birth / Date of
                                Incorporation, followed by Captcha.</p>
                        </li>
                        <li>
                            <p>Select the ‘My Account’ option and then click on "Refund/Demand Status" to view your
                                income tax refund status, along with other important details like reason for the delay
                                in refund (if any), the assessment year and the mode of payment opted for.</p>
                        </li>
                    </ul>
