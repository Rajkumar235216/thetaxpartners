                        <h4><strong>When is notice under section 142(1) issued?</strong></h4>
                        <p>Notice under section 142(1)&nbsp;can be issued in both the cases where:</p>
                        <ol>
                            <li>return has been filed and</li>
                            <li>where return has not been filed and time specified to file such return has been expired.
                            </li>
                        </ol>
                        <h4><strong>Why is notice under section 142(1) issued?</strong></h4>
                        <p>This notice is issued to the assessee:</p>
                        <ol>
                            <li>to file income tax return if the same is not filed within the time limit specified.</li>
                            <li>to produce specific accounts and documents such as sales register, purchase register,
                                voucher, receipts / proof of deductions availed, invoices of expenses made etc.</li>
                            <li>to produce other details and documents as the Assessing Officer thinks fit.</li>
                        </ol>
                        <h4><strong>How to deal with notice under section 142(1)?</strong></h4>
                        <ol>
                            <li>When the notice is issued requiring the assessee to file the return, the same should be
                                filed within the time specified in the notice. A proof of the same is required to be
                                submitted to the Assessing Officer.</li>
                            <li>If the return has been filed and certain documents and details are asked for, the same
                                should be submitted to the Assessing Officer within the time specified in the notice.
                            </li>
                        </ol>
                        <p>Assessee is given a reasonable opportunity of being heard for any matter that has been
                            gathered on the basis of such inquiry.</p>
                        <h4><strong>Consequences of failure to comply with the notice under section
                                142(1)</strong>&nbsp;</h4>
                        <p>If the notice under section 142(1) is not complied with:</p>
                        <ol>
                            <li>it may result in Best Judgement Assessment under section 144 (the assessing officer will
                                make the judgement and proceed the case on his own)</li>
                            <li>penalty of Rs. 10,000/- may be levied for each failure</li>
                            <li>imprisonment up-to one year along with fine may be imposed.</li>
                        </ol>
                    

