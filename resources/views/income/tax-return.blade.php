        <h4>FAQs on Income Tax Return</h4>
        <h5>I am a self-employed professional. My income this year is below the minimum income tax slab, but it was
        higher in the previous 2 years and I had filed ITRs then. Do I have to file a return this year?"</h5>
        <p>Ideally, if your income is below the minimum slab, you do not need to file your income tax return. But
        if you have been filing it previously, it would be better if you file a return this year declaring low income.
        This is because the Income Tax Department may construe your non-filing as a delay or non-compliance because of
        your past record of filing ITR. They might send you a notice. In case you do not want to file ITR, be prepared
        to reply to the income tax notice if it comes into your mail box.
        </p>
        <h5>If I have already paid advance taxes and have no dues or refunds, can I skip filing of income tax
        return?</h5>
        <p>No, you have to file your ITR especially if you have paid the taxes. It is not an optional activity. An
        ITR gives the government a complete record of how your income is distributed – assets, total income, tax
        liability, tax paid and refunds. This helps them monitor people for tax fraud of any kind. If you fail to file
        ITR, you are liable to pay penalty or face scrutiny and prosecution.</p>
        <h5>Is an ITR useful to me in daily life?</h5>
        <p> Of course, yes. You’ll find a duly-filed ITR very useful as a proof of income when you have to apply for
        a bank loan, when you have to make accident claims in third party insurance, for immigration and visa, for
        appointment in judicial and class 1 jobs, for winning government tenders, for registration in professional
        panels, for seeking funding for a startup, etc.</p>
        <h5>How long do we need to wait to get income tax refund?</h5>
        <p>Refunds are disbursed only after your ITR is processed. This may take up to 1 year after you file your
        return. It is also possible that even if you are claiming refund, the taxman may have ascertained that you are
        not liable for a refund for various reasons such as a calculation error on your part. If you think your
        calculations are right and you have to get a refund, then you can write to the I-T Department again. Sometimes,
        you do not get a refund because of wrong PAN or bank account number on the ITR, or the cheque not reaching you
        because of wrong address or you not being at home when the postman reached. If you filed your ITR online, you
        can check the status of your refund through your Income Tax e-filing account.</p>
        <h5> Which is the best way to file Income Tax Return?</h5>
        <p>There are multiple ways of filing Income Tax Return. You can file your ITR yourself or take the help of
        a chartered accountant. The easiest way is to file your tax online yourself. You can do so on the website of the
        Income Tax Department. All you have to do is fill the relevant columns on the ITR form and submit it. You can
        also choose to file your ITR using any third-party e-filing website as well. If you find it difficult to file
        your ITR yourself and need expert advice, then it is best to take the help of a chartered accountant.
       </p>
        <h5>Can I File Income Tax Return after Due Date?</h5>
        <p>Yes, you can file your Income Tax Return after the due date. Due to unavoidable circumstances sometimes
        people are unable to file their ITR by the due date. In such cases, they have the option of filing a belated
        return under Section 139 (4) before the end of Assessment Year or before the Assessment is completed (whichever
        is earlier)</p>
        <h5>How to Check the Status of Income Tax Return?</h5>
        <p>Once you file your Income Tax Return, you can check the status of Income Tax Refund on the website of
        the Income Tax Department of India. You can check the status after 10 days, starting from the date the refund is
        sent by entering your Permanent Account Number and selecting the Assessment Year.</p>
        <h5>Which form should use to file Income Tax Return for Salaried Individuals?</h5>
        <p>Individuals who earn income from salary and interest have to fill and submit ITR 1 (SAHAJ) form while
        filing their Income Tax Return.</p>
        <h5>What is Income Tax Return Notice?</h5>
        <p>If you do not file your Income Tax Return in an Assessment Year, then you may receive a notice from the
        Income Tax (IT) Department. The IT Department keeps a track on financial transactions and activities of
        individuals who are identified as non-filers. If there is a discrepancy with the amount of tax that you have
        declared in your ITR, then you may receive a notice from the IT Department. You may also receive a notice if the
        department wants to review any documents related to the ITR that you have filed.</p>
