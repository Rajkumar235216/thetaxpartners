        <h4>Importance of ITR Filing</h4>
        <p>Currently(FY 2019-20), is mandatory for one to file income tax returns in India if the following conditions
            are applicable -</p>
        <ul>
            <li>If the gross total annual income (before deductions under 80C to 80U) is Rs. 2,50,000 (for ages less
                than 60 years), Rs. 3,00,000 (for ages 60 years but less than 80 years) and Rs. 5,00,000 (for ages 80
                years and above)</li>
            <li>If it’s a company or firm, irrespective of the profit or loss made in a financial year</li>
            <li>If a tax refund needs to be claimed</li>
            <li>If a loss under a head of income needs to be carried forward</li>
            <li>If being a resident of India, one has an asset or financial interest in any entity located outside India
            </li>
            <li>If being a resident of India, one is a signing authority in a foreign account</li>
            <li>If one receives income derived from property held under a trust for charitable or religious purposes or
                a political party or a research association, news agency, educational or medical institution, trade
                union, a not for profit university or educational institution, a hospital, infrastructure debt fund, any
                authority, body or trust</li>
            <li>If one is applying for a loan or a visa</li>
            <li>If an NRI derives any or all of his/her income through sources in India, that income is liable to be
                taxable in India, and income tax returns for the same will be necessary.</li>
        </ul>
        <p><strong>With the implementation of e-filing of Income Tax Returns, the following cases will require an
                e-filing Income Tax:</strong> </p>
        <ul>
            <li>In case a refund is required</li>
            <li>In case the gross total annual income exceeds Rs. 5,00,000</li>
            <li>In case an income tax refund is required</li>
            <li>ITR-3, 4,5,6,7 have to be mandatorily e-filed</li>
        </ul>
        <h4>Income Tax E-filing Due Date</h4>

        <p>The due date of E-filling of income tax returns depends upon the taxpayer. If the taxpayer is individual,
            body of individuals, association of persons, Hindu undivided family then the due date for filling income tax
            return is August 31 every financial year. However, for business and companies which require audit the date
            for filling income tax return is September 30 every financial year. For any person who is required to
            furnish report in form No. 32CEB under section 92E the date for filling income tax return is November 30
            every financial year. If the taxpayer does not file the income tax return in time, then he will have to pay
            the late fee along with the tax payable.</p>

