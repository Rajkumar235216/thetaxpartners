        <h4>Introduction on Income Tax</h4>

        <p>In India, there are two types of taxes one is direct tax which is directly levied upon the income of the
            individual and another is indirect tax which is levied indirectly upon an individual. Example of direct tax
            is income tax and of indirect tax is Goods and Services Tax, service tax etc.</p>
        <p>Every individual who receives income in India is subject to income tax under the Income Tax Act, 1961. Under
            Income Tax Act there can be income from 5 different heads which are Income from salary, house property,
            capital gains, business and profession and other sources.Income of individuals, Hindu undivided family,
            association of person, body of individuals firms and companies are taxed under the Income tax act.</p>



        <h4>What is Income Tax Return</h4>

        <p>Income Tax Return is a form in which the taxpayer whether it be an individual or firm or Hindu undivided
            family, discloses details of its income, claims exemptions and deductions which are applicable on it and the
            amount of tax payable on such income. The Income Tax Return also reflects the amount of taxes paid by the
            taxpayer.</p>
        <p>Income tax return is form in which the taxpayer files data and information regarding his income and tax
            payable thereon at the end of every financial year.</p>

        <h4>What is Income Tax eFiling</h4>

        <p>Income Tax can be filed by the two way i.e is the Online Method and another offline Method. Now as per the
            Government Instructions all the income tax return will be filed only through ITR eFiling Method.</p>
        <p>So in the following cases, ITR Online eFiling is Mandatory –<br>
            1) if your taxable income is more than 5 lakh rupees in a year or if you want to claim any refund.<br>
            2) if your accounts will required auditing under section 44 AB and Return would be furnished in ITR 3 or ITR
            4</p>
        <p>Only these person can use the offline ITR Filing Method –<br>
            1) if your taxable income is less than 5 lakh rupees in the year and not required to claim any refund.<br>
            2) An individual who’s age 80 years or more during the filing of Income Tax Return.</p>


