        <h2>What is e-assessment scheme or faceless scrutiny?</b></h2>
        <p><span style="font-weight: 400;">In the Union Budget speech 2019-20, the Finance Minister Nirmala Sitharaman
                announced the </span><b>faceless e-assessment scheme</b><span style="font-weight: 400;">. As per the
                scheme, </span><span style="font-weight: 400;">scrutiny assessment</span><span
                style="font-weight: 400;"> from 2019 onwards will be carried out via an electronic mode with no human
                interface. Thus, there will be no face to face interaction between the taxpayer and the Income Tax
                Department when a taxpayer’s ITR is picked for a detailed scrutiny. This is expected to make the process
                of scrutiny more fair and unprejudiced, thereby decreasing the chances of undesirable practices on the
                part of tax officials.</span><span id="2"></span></p>
        <h2><b>What is scrutiny assessment?&nbsp;</b></h2>
        <p><span style="font-weight: 400;">The Income Tax Department not only reviews the income tax return (ITR) filed
                by the taxpayers, but also monitors their ITR filing patterns. Based on this, the Department selects
                some cases for a detailed examination along with some randomly picked cases. This process of detailed
                examination of one’s ITR is called Income Tax scrutiny. An </span>income tax notice under Section 143(2)</span><span
                style="font-weight: 400;"> of the Income Tax Act is sent to such taxpayers to inform that their ITR has
                been picked for scrutiny.</span><span id="3"></span></p>
        <h2><b>What does communication via electronic mode mean?</b></h2>
        <p><span style="font-weight: 400;">All communications between the National e-assessment Centre and the assessee,
                or his authorized representative, will be made exclusively by electronic mode under the e-assessment
                scheme.</span></p>
        <p><span style="font-weight: 400;">Moreover, all internal communications between the National e-assessment
                Centre, Regional e-assessment Centers and various units shall be exchanged exclusively by electronic
                mode.</span><span id="4"></span></p>
        <h2><b>How will the electronic records be delivered?</b></h2>
        <p><span style="font-weight: 400;">First, all the electronic records will be authenticated by affixing digital
                signature or electronic authentication technique. Subsequently, it will be delivered by way of:</span>
        </p>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Placing a copy to the assessee’s registered
                    account; or</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Sending a copy to the assessee’s or his
                    representative’s registered email address; or</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Uploading a copy on the assessee’s Mobile
                    App</span></li>
        </ul>
        <p><b>Note</b><span style="font-weight: 400;">: A real-time alert will be sent to the assessee or his/her
                authorized representative at every point of communication. The assessee will have to respond to such a
                communication through his/her registered account only. Finally, an acknowledgment will be received upon
                successful submission of response which will deem the response to be authenticated.</span><span
                id="5"></span></p>
        <h2><b>Procedure for faceless e-assessment</b></h2>
     
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">A notice under section 143(2) of Income Tax
                    Department will be sent by National e-assessment Centre. The notice will specify the issues for
                    selection of the taxpayer’s case for scrutiny.</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">The taxpayer/assessee may respond to the
                    National e-assessment Center within 15 days of the date of the receipt of the notice.</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">The National e-assessment Centre will assign
                    the case to one of the regional e-assessment centers through an automated allocation system.</span>
            </li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">The regional e-assessment center may request
                    the National e-assessment center to:</span><b>(i) Obtain necessary information or documents or
                    evidence from the assessee: <span style="font-weight: 400;">The National e-assessment center will
                        issue an appropriate notice to the assessee for obtaining the relevant information.<br>
                    </span></b><br> <strong>(ii) Conduct enquiry or verification by the verification unit:</strong>
                <span style="font-weight: 400;">The National e-assessment center will assign the request to a
                    verification unit via an automated allocation system.</span><strong>(iii) Seek technical assistance
                    from the technical unit:</strong> <span style="font-weight: 400;">The National e-assessment center
                    will assign the request to a technical unit in any of the Regional e-assessment centers via an
                    automated allocation system.</span></li>
            <li><b><span style="font-weight: 400;">The assessment unit after taking into account all the information
                        will prepare a </span>draft assessment order<span style="font-weight: 400;"> either accepting or
                        modifying the ITR of the assessee. A copy of this order will be sent to the National
                        e-assessment center.</span></b></li>
        </ul>
        <p><b>Note</b><span style="font-weight: 400;">: The assessment unit will provide the details of the penalty
                proceedings in the assessment order sent to the National e-assessment center.</span></p>
        <ul>
            <li><b><b><span style="font-weight: 400;">The National e-assessment center will examine the draft assessment
                            order via an automated examination tool in accordance with the approved risk management
                            strategy and may:</span></b></b><b>(i) Finalize <span style="font-weight: 400;">the draft
                        assessment order and issue a notice to the assessee to initiate penalty proceedings along with a
                        demand or refund notice, if applicable.<br> </span></b><span style="font-weight: 400;"><br>
                    <strong>(ii)</strong> Give the assessee a chance by sending a notice to call him/her to </span>show
                cause<span style="font-weight: 400;"><span style="font-weight: 400;"><span id="6"> as to why the
                            assessment should not be completed as per the draft assessment order (when a modification is
                            proposed).</span></span><span style="font-weight: 400;"><strong>(iii)</strong> Assign the
                        draft assessment order to one of the </span>review <span style="font-weight: 400;">units in any
                        Regional e-assessment centers via an automated allocation system.</span></span></li>
        </ul>
        <p>&nbsp;</p>
        <h2><b>What to do when a show-cause notice is issued after the e-assessment?</b></h2>
        <p><span style="font-weight: 400;">When a show-cause notice is issued to an assessee, he/she should furnish the
                response within the specified time limit.</span></p>
        <p><span style="font-weight: 400;">The assessment unit after receiving the response from the assessee will
                prepare a revised draft assessment order and send it to the National e-assessment center which
                may:</span></p>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Finalize the assessment as per the revised
                    draft assessment order and serve a copy of the order and the notice to the assessee for initiating
                    penalty proceedings; or</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Send another show-cause notice to the
                    assessee, if a modification is proposed.</span></li>
        </ul>
        <p><span style="font-weight: 400;">However, when an </span><b>assessee fails to submit the response </b><span
                style="font-weight: 400;">within the specified time limit, the National e-assessment centre will
                finalize the assessment as per the initial draft assessment order submitted by the Assessment
                Unit.</span><span id="7"></span></p>
        <h2><b>What happens after Review Unit receives the draft assessment order?</b></h2>
        <p><span style="font-weight: 400;">This happens when the National e-Assessment Center sends the draft assessment
                order to one of the Review Units in any Regional e-assessment Center via an automated allocation
                system.</span></p>
        <p><span style="font-weight: 400;">Subsequently the Review Unit will review the order and may:</span></p>
        <ol>
            <li style="font-weight: 400;"><b>Agree with the draft assessment order</b><span style="font-weight: 400;">:
                    The National e-assessment center will finalize the draft assessment order. Alternatively, if a
                    modification is proposed, the center will send a show-cause notice to the assessee.</span></li>
            <li style="font-weight: 400;"><span id="8"><b>Suggest modification to the draft assessment order: </b><span
                        style="font-weight: 400;">After considering the modifications, the assessment unit will send the
                        final assessment order to the National e-assessment center. The center may finalize the order or
                        send a show-cause notice to the assessee, if a modification is proposed.&nbsp;</span></span>
            </li>
        </ol>
        <p>&nbsp;</p>
        <h2><b>What happens after completion of e-assessment?</b></h2>
        <p><span style="font-weight: 400;">After finalizing the draft assessment order, the National e-assessment center
                will transfer all the electronic records to the Assessing Officer (AO) having jurisdiction over the
                case. Then the AO may take the following actions:&nbsp;</span></p>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Imposition of penalty;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Collection and recovery of tax demand;</span>
            </li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Rectification of mistake;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Giving effect to appellate orders;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Submission of remand report or any
                    representation to be made or any record to be produced before the Commissioner, Appellate Tribunal
                    or Courts;&nbsp;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Proposal seeking sanction for launch of
                    prosecution;</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Any other reason considered to be appropriate
                    by National e-assessment Center&nbsp;</span></li>
        </ul>
        <p><b>Note: The penalties, if applicable will be imposed as per the respective sections of the Income Tax Act,
                1961.</b><span id="9"></span></p>
