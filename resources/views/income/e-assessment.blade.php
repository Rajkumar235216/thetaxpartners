        <h2><b>Frequently Asked Questions (FAQs)</b></h2>
        <p><strong>1. What is the E-assessment Scheme, 2019?</strong></p>
        <p><span style="font-weight: 400;">Ans. The Finance Minister, Nirmala Sitharaman announced faceless e-assessment
                scheme in the Union Budget 2019-20. As per the scheme, </span>scrutiny assessment</span><span style="font-weight: 400;"> from 2019
                will be carried out via an electronic mode with no human interface. In other words, there will be no
                personal i.e. face to face interaction between the taxpayer and the Income Tax Department when a
                taxpayer’s ITR is picked for detailed scrutiny.&nbsp;</span></p>
        <p><strong>2. What is scrutiny assessment?</strong></p>
        <p><span style="font-weight: 400;">Ans. The Income Tax Department reviews the income tax return (ITR) filed by
                the taxpayers, but also monitors their ITR filing patterns. Based on this, the Department selects some
                cases for a detailed examination along with some randomly picked cases. This process of detailed
                examination of filed returns is called </span>Income Tax Scrutiny</span><span style="font-weight: 400;">.
                An</span> income tax notice under Section 143(2)</span><span
                style="font-weight: 400;"> of the Income Tax Act is sent to such taxpayers to inform that their ITR has
                been picked up for scrutiny.</span></p>
        <p><strong>3. How should I reply to the income tax notice u/s 143(2)?</strong></p>
        <p><span style="font-weight: 400;">Ans. If your ITR has been picked for a detailed assessment or scrutiny, you
                will receive a notice under Section 143(2) via an electronic mode. Upon receiving such a notice, you
                should respond within 15 days through your registered account only. An acknowledgment will be received
                upon successful submission of response, that will deem the response to be
                authenticated.&nbsp;&nbsp;</span></p>
        <p><strong>4. How will electronic communications be delivered under e-assessment scheme?</strong></p>
        <p><span style="font-weight: 400;">Ans. Electronic communications under the e-assessment scheme will be
                delivered by:</span></p>
        <ul>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Placing a copy to the assessee’s registered
                    account; or</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Sending a copy to the assessee’s or his
                    representative’s registered email address; or</span></li>
            <li style="font-weight: 400;"><span style="font-weight: 400;">Uploading a copy on the assessee’s <a
                        href="https://www.paisabazaar.com/tax/aaykar-setu-income-tax-mobile-app/">Mobile App</a></span>
            </li>
        </ul>
        <p><span style="font-weight: 400;">Moreover, a real-time alert will be sent to the assessee or his/her
                authorized representative at every point of communication.</span></p>
        <p><strong>5. How will electronic communications be authenticated under the faceless scrutiny assessment
                scheme?</strong></p>
        <p><span style="font-weight: 400;">Ans. Under the e-assessment scheme, all electronic communications must be
                authenticated by affixing digital signature or electronic authentication technique. Thus, whenever you
                send a response to the IT Department, authenticate it with your digital signature.</span></p>
        <p><strong>6. How is faceless e-assessment different from the earlier system?</strong></p>
        <p><span style="font-weight: 400;">Unlike the previous method, e-assessment involves communication via
                electronic mode only. There is no personal interaction between the assessee and the department. This is
                expected to eliminate undesirable practices on the part of tax officials. For more info, read the
                Differences between the new e-Assessment.&nbsp;&nbsp;&nbsp;&nbsp;</span></p>
        <p><strong>7. What will be the advantage of the e-assessment scheme?</strong></p>
        <p><span style="font-weight: 400;">Ans. Under the e-assessment scheme, there will be no personal interaction
                between the assessee and the assessing officer. Also, the Assessing Unit will be selected by an
                automated system without any bias. Thus, chances of any undesirable practices on the part of tax
                officials will decrease making the process of scrutiny assessment more fair and just.&nbsp;</span></p>
        <p><strong>8. What effect will e-assessment have on taxpayers?</strong></p>
        <p><span style="font-weight: 400;">Ans. As the mode of communication under the e-assessment scheme will be
                solely electronic, the taxpayer will no longer be required to post documents or personally meet the
                assessing officer to answer any queries.&nbsp;</span></p>
        <p><strong>9. What to do when a show-cause notice is issued?</strong></p>
        <p><span style="font-weight: 400;">Ans. A show-cause notice is sent when a modification is proposed in the
                previously filed ITR. Upon receiving this notice, you should reply within the specified time limit from
                your registered account. Remember to authenticate the response by affixing the digital signature. Also,
                make sure that you receive an acknowledgement upon successful submission of the response.</span></p>
        <p><strong>10. How will the penalties be imposed after the e-assessment?</strong></p>
        <p><span style="font-weight: 400;">Ans. After successful e-assessment, a copy of the final draft assessment
                order is sent to the Assessing Officer having jurisdiction over such case. Subsequently, penalties will
                be imposed as per the chapter XXI of the Income Tax Act. Relevant notices will be sent to the assessee
                in such a case.</span></p>

