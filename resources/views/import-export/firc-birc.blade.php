        <h2><strong><u>What is FIRC</u></strong></h2>
        <p>FIRC (Foreign Inward Remittance Certificate) is a document that is issued by a bank to their customers
            against all the inward remittances entering India. It can be an advance payment against export proceeds,
            ocean or airfreight, or remuneration or wages under consultancy charges or for any other reasons.</p>
        <h2><strong><u>What is e FIRC</u></strong></h2>
        <p>e FIRC is used to denote an FIRC which is issued by a bank in EDPMS (<a
                href="https://www.rbi.org.in/Scripts/NotificationUser.aspx?Id=11119&amp;Mode=0">Export Data Processing
                and Monitoring System)</a>, after the funds have been credited into a customer’s account.</p>
        <h2><strong><u>What is the difference between BRC and FIRC?</u></strong></h2>
        <p>Both BRC &amp; FIRC are certificates issued by authorized dealer banks to customers for receiving amounts
            from foreign countries.</p>
        <p>FIRC (Foreign Inward Remittance Certificate) is issued against any receipt of amount from foreign countries
            by a bank to their customers. It can be an advance payment against export proceeds, ocean or airfreight, or
            remuneration or wages under consultancy charges or for any other reasons.</p>
        <p>BRC (Bank Realization Certificate) is issued by banks to its customers who run export businesses. BRC is
            issued on each shipment of export proceeds. Various export promotion agencies provide incentives, import
            duty exemptions and other financial assistance to the exporters. These agencies require export proof to be
            submitted by exporters to claim such benefits. One such proof of export is Bank Realization Certificate BRC
            issued by the respective bank who received foreign amount for exporters.</p>
        <h2><strong><u>What are the uses of FIRC</u></strong></h2>
        <p>FIRC is used by most statutory authorities as a document of proof that an individual has received a payment
            in foreign currency from outside the country. FIRC is considered a very important document as it serves a
            lot of purposes. If shares are issued in the name of a person or company which exists outside a country then
            FIRC acts as the proof of money received in lieu of share application.</p>
        <p>In those cases where a resident Indian sells or transfers his shares to some non-resident Indian or foreign
            identity then FIRC testifies that the resident seller has got the share purchase consideration. FIRC is a
            very crucial document which is submitted to DGFC in case of EPCG and Advance License.</p>
        <p>When payment is received for export of services then no GST is levied according to the rules of export of
            services. In such cases, FIRC acts as an important proof of export of services and remittances which are
            received in lieu of them.</p>
        <h2><strong><u>How are payments routed to beneficiary’s account?</u></strong></h2>
        <p>When an individual receives some amount of money from a foreign country then it is credited to his account
            through banks authorized to do so by the Reserve Bank of India. If the beneficiary does not have a bank
            account in an authorized bank then he cannot get his money transferred into his bank account.</p>
        <h2><strong><u>Details contained in FIRC</u></strong></h2>
        <p>A Foreign Inward Remittance Certificate – FIRC contains the following details-</p>
        <ul>
            <li>Beneficiary’s name</li>
            <li>Whether the money has been paid by cash or by crediting the account of the beneficiary</li>
            <li>Name and the address of the person being remitted</li>
            <li>Name and address of the authorized bank remitting the amount</li>
            <li>DD / TT No / Cheque No</li>
            <li>The foreign direct investment amount in the foreign currency</li>
            <li>The equivalent amount in rupee (should be in figures as well as words)</li>
            <li>The name of the person in who’s favor the amount has been remitted</li>
            <li>The prevalent exchange rate which has been applied</li>
            <li>What the purpose of the respective remittance is as stated by the beneficiary</li>
        </ul>
        <p>The FIRC has to be signed by the authorized signatory of the authorized designated bank and is further
            countersigned by one more person. As per procedure, the Foreign Inward Remittance Certificate has to be
            issued to the address of the account holder, which normally has to be within a period of not more than
            fifteen days from the date on which the funds were credited to the beneficiary’s account in the authorized
            bank.</p>
        <p>In cases where the inward remittance has been transferred into the beneficiary’s account through more than
            one bank account, the first bank that received the inward remittance in convertible foreign currency must
            issue the Foreign Inward Remittance Certificate first, as it would have the requisite details of the
            overseas bank which have remitted the foreign exchange. This clarification was made in RBI notifications and
            circulars as well as the provisions under FEMA.</p>
        <h2><strong><u>Conclusion</u></strong></h2>
        <p>Therefore, the Foreign Inward Remittance Certificate (FIRC) is of great importance when it comes to
            remittances received from banks, which are overseas, and not in India. Thus, it is necessary that the
            beneficiaries make it a point and priority to follow up with the banks and get their Foreign Inward
            Remittance Certificate issued as soon as possible after the inward remittance has been credited. Also, it is
            necessary to check the purpose of foreign direct investment, as any error in the mentioning of this has
            serious implications and consequences when it comes to remittance, its usage, and accounting of the same.
        </p>


