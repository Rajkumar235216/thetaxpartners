<h2 id="Benefits"><strong>Benefits of IEC Registration</strong></h2>
<h3 style="padding-left: 30px;"><b>1. Expansion of Business</b></h3>
<p style="padding-left: 30px;"><span style="font-weight: 400;">IEC assists you in taking your services or product to the global market and grow your businesses.</span></p>
<h3 style="padding-left: 30px;"><b>2. Availing Several Benefits</b></h3>
<p style="padding-left: 30px;"><span style="font-weight: 400;">The Companies could avail several benefits of their imports/ exports from the DGFT, Export Promotion Council, Customs, etc., on the basis of their IEC registration.</span></p>
<h3 style="padding-left: 30px;"><b>3. No Filling of returns</b></h3>
<p style="padding-left: 30px;"><span style="font-weight: 400;">IEC does not require the filing of any returns. Once allotted, there isn’t any requirement to follow any sort of processes for sustaining its validity. Even for export transactions, there isn’t any requirement for filing any returns with DGFT.</span></p>
<h3 style="padding-left: 30px;"><b>4. Easy Processing</b></h3>
<p style="padding-left: 30px;"><span style="font-weight: 400;">It is fairly easy to obtain IEC code from the DGFT within a period of 10 to 15 days after submitting the application. There isn’t any need to provide proof of any export or import for getting IEC code.</span></p>
<h3 style="padding-left: 30px;"><b>5. No need for renewal</b></h3>
<p style="padding-left: 30px;"><span style="font-weight: 400;">IEC code is effective for the lifetime of an entity and requires no renewal. After it is obtained, it could be used by an entity against all export and import transactions.</span></p>
<h2 id="Cases"><strong>Cases where Export Import Code (EIC) is not mandatory</strong></h2>
<p><span style="font-weight: 400;">According to the</span><span style="font-weight: 400;"> latest circular issued by the government</span>, IEC is not mandatory for all traders who are registered under GST. In all such cases, the <span style="font-weight: 400;">PAN of the trader shall be construed as new IEC code for the purpose of import and export.</span></p>
<p><span style="font-weight: 400;">Import Export Code (IEC) isn’t required to be taken in case the goods exported or imported is for personal purposes and isn’t used for any commercial purpose.</span></p>
<p><span style="font-weight: 400;">Export/ Import done by the Government of India Departments and Ministries, Notified Charitable institutions need not require getting Import Export Code.</span></p>
