        <h1 class="mt0">
            IEC (Import Export Code) – How to Apply for It, Benefits
          </h1>
          <p class="subtitle"></p>
          <p class="timestamp mt0">Updated on <time datetime="2019-11-27T13:27:03">Nov 27, 2019 - 01:27:03 PM</time></p>

          
          

          <div class="row">
            <div class="col-xs-12 col-sm-12 col-md-8">
              
              <div class="pb40">
                <p><span style="font-weight: 400;">In this age of cut-throat competition, everyone wants to grow their business beyond the limits of the domestic market. However, doing business globally isn’t just a cup of tea for everyone. Before going global, you need to follow several procedures and laws in place and get different registration and license. IEC (Import Export Code) license is one of such prerequisite when you’re thinking of importing or exporting from India.</span></p>
<p>&nbsp;</p>
<div style="padding: 40px; margin: 20px 0; border: 1px solid #ccc; background: #fafafa; text-align: justified;">
<ul>
<li><a href="#IEC">When is IEC Required </a></li>
<li><a href="#Applying">Import/Export Code- Registration Steps</a></li>
<li><a href="#Documents">Documents&nbsp;Required for IEC Registration</a></li>
<li><a href="#Benefits">Benefits of IEC Registration</a></li>
<li><a href="#Cases">Cases where Export Import Code is not mandatory</a></li>
</ul>
</div>
<p>&nbsp;</p>
<p>IEC (Import Export Code) is required by anyone who is looking to kick-start his/her&nbsp;import/export business&nbsp;in the country. It is issued by the DGFT (Director General of Foreign Trade). IEC is a 10-digit code which has a lifetime validity. Predominantly importers merchant cannot import goods without the Import Export Code and similarly, the exporter merchant cannot avail benefits from DGFT for the export scheme, etc. without IEC.</p>
<h2 id="IEC"><strong>IEC is required in the following situations</strong></h2>
<p><span style="font-weight: 400;">1. When an importer has to clear his shipments from the customs then it’s needed by the customs authorities.</span></p>
<p><span style="font-weight: 400;">2. When an importer sends money abroad&nbsp;through banks then it’s needed by the&nbsp;bank.</span></p>
<p><span style="font-weight: 400;">3. When an exporter has to send his shipments then its needed by the customs port.</span></p>
<p><span style="font-weight: 400;">4. When an exporter receives money in foreign currency directly into his bank account then its required by the bank.</span></p>
<h2 id="Applying"><strong>Steps Involved in IEC (Import/Export Code) Registration</strong></h2>
<p><span style="font-weight: 400;">Step 1: First, you need to prepare an application form in the specified format – Aayaat Niryaat Form no. 2A format and file it with the respective Regional office of DGFT.</span></p>
<p><span style="font-weight: 400;">Step 2: Secondly, you need to prepare the required documents with respect to your identity &amp; legal entity and address proof with your bank details &amp; the certificate in respect of ANF2A.</span></p>
<p><span style="font-weight: 400;">Step 3: One your application is completed, you need to file with DGFT via DSC (Digital Signature Certificate) and pay the required fee for the IEC Registration.</span></p>
<p><span style="font-weight: 400;">Step 4: Finally, once your application is approved then you would receive the IEC Code in a soft copy from the government.</span></p>
<h2 id="Documents"><strong>Documents&nbsp;required for IEC (Import Export Code) Registration</strong></h2>
