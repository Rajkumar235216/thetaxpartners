        <h4 >What is disinvestment?</h4>

        <p>In business, disinvestment means to sell off certain assets such as a manufacturing plant, a division or
            subsidiary, or product line. Disinvestment is sometimes described as the opposite of capital expenditures.
            Some people use the term <em>divestiture</em>, or <em>to divest</em> when discussing disinvestment.</p>
        <p>For example, an electric generator manufacturer might sell off its consumer generator product lines and
            manufacturing facilities in order to raise money that can be used to expand its industrial generator product
            line.</p>
        <p>Another example is a consumer products company selling off a profitable division that no longer meets its
            long range goals. The proceeds from this disinvestment are then used to improve the company's financial
            position by reducing its debt.</p>


        <p><strong>Disinvestment in India meaning:</strong> Disinvestment means sale or liquidation of assets by the
            government, usually Central and state public sector enterprises, projects, or other fixed assets. The
            government undertakes disinvestment to reduce the fiscal burden on the exchequer, or to raise money for
            meeting specific needs, such as to bridge the revenue shortfall from other regular sources. In some cases,
            disinvestment may be done to privatise assets. However, not all disinvestment is privatisation. Some of the
            benefits of disinvestment are that it can be helpful in the long-term growth of the country; it allows the
            government and even the company to reduce debt. Disinvestment allows a larger share of PSU ownership in the
            open market, which in turn allows for the development of a strong capital market in India.</p>
        <h2>Main objectives of Disinvestment in India:</h2>
        <ul>
            <li>Reducing the fiscal burden on the exchequer</li>
            <li>Improving public finances</li>
            <li>Encouraging private ownership</li>
            <li>Funding growth and development programmes</li>
            <li>Maintaining and promoting competition in the market</li>
        </ul>
        <h2>Are disinvestment and privatisation related?</h2>
        <p>The government, whenever it so desires, may sell a whole enterprise, or a majority stake in it, to private
            investors. In such cases, it is known as privatisation, in which the resulting ownership and control of the
            organisation does not rest with the government. The government usually avoids doing this. The government
            mostly retains more than half of the stake in the public sector enterprise so that the control remains in
            its hands. But when it doesnâ€™t, then the ownership is transferred to the private sector, which results in
            privatisation. It is also known as majority disinvestment or complete privatisation wherein 100 per cent
            control goes to the private sector.</p>

        <h2>What do you mean by disinvestment policy of government of India?</h2>
        <p>There is a separate department under the Ministry of Finance which handles all disinvestment-related works
            for the government. On 10 December 1999, the Department of Disinvestment was set up as a separate department
            and later renamed as Department of Investment and Public Asset Management. Disinvestment targets are set
            under each Union Budget, and every year the targets change. The government takes the final decision on
            whether to raise the divestment target or not.</p>
        <p>Since the late 1990s, disinvestment has become an almost regular feature of the Union budgets under
            successive governments, which set a target each year to raise funds from stake sales in public sector
            enterprises. Disinvestment has led to mixed results for the governments in terms of meeting the revenue
            targets. Governments select disinvestment candidates based on various factors, such as its existing stake in
            the company, private sector interest in ownership of that enterprise, general market conditions, expected
            value realisation etc.</p>

