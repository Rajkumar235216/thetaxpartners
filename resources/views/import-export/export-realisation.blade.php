            <p>   <span id="ContentPlaceHolder1_contenttxt"></span></p><p align="justify"><strong><span style="color: #2b4f15; font-size: xx-large;">Procedure for Realisation of Export Proceeds<br>&nbsp;</span></strong></p>
<p align="justify">The following is the Procedure for the realisation of export proceeds:</p>
<p align="justify">(a) Presentation of Documents to the Bank for Negotiation: After shipment of goods, the exporter is required to submit the shipping documents to an authorised dealer Within 21 days of the date of Shipment for negotiation, Submission of relevant documents to the bank and process of getting the payment from the bank is called "Negotiation of the Documents” and the documents are called 'Negotiable Set of Documents'. The set normally <img class="img-responsive" style="float: right;" src="/UserFiles/cmspageimgs/4622I-010518154400.jpg" alt="">contains:</p>
<p align="justify">Bill of Exchange, Sight Draft or Usance Draft.</p>
<p align="justify">Full set of Bill of Lading or Airway Bill.</p>
<p align="justify">Original Letter of Credit.</p>
<p align="justify">Customs Invoice.</p>
<p align="justify">Commercial Invoice including one Copy duly Certified by the Customs.</p>
<p align="justify">Packing List.</p>
<p align="justify">Foreign exchange declaration forms, GR/SOFTEX/PP forms in duplicate.</p>
<p align="justify">Exchange control copy of the Shipping Bill.</p>
<p align="justify">Certificate of Origin, GSP or APR Certificate, etc.</p>
<p align="justify">Marine Insurance Policy, in duplicate.</p>
