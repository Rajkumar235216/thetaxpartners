<p align="justify">(b) <strong>Despatch of Documents:</strong></p>
<p align="justify">The bank negotiates these documents to the importer's bank in the manner as specified in the L/C. Before negotiating documents, the exporter's bank scrutinises them in order to ensure that alt formalities have been complied with and all documents are in order. The bank then sends the Bank Certificate and attested copies of commercial invoice to the exporter.<br>&nbsp;</p>
<p align="justify"><strong>(c) Acceptance of the bill of exchange:</strong> Bill of exchange accompanied by the above documents is known as the Documentary Bill of Exchange. It is of two types:<br>&nbsp;</p>
<p align="justify">&nbsp;<strong>Documents against Payment (Sight Drafts):</strong></p>
<p align="justify">In case of sight draft, the drawer instructs the bank to hand over the relevant documents to the Importer only against payment.</p>
<p align="justify">&nbsp;</p>
<p align="justify">&nbsp;<strong>Documents against Acceptance (Usance Draft):</strong></p>
<p align="justify">In case of usance draft, the drawer instructs the bank to hand over the relevant documents to the importer against his 'acceptance' of the bill of exchange.<br>&nbsp;</p>
<p align="justify">(a) <strong>Letter of Indemnity:</strong> The exporter can get immediate payment from his bathe on the submission of documents by signing a letter of indemnity. By signing letter of indemnity the exporter undertakes to indemnify the bank in the event of non-receipt of payment from the importer along with accrued interests.<br>&nbsp;</p>
<p align="justify">(e) <strong>Realisation of Export Proceeds:</strong> On receiving the documentary bill of exchange, the importer releases payment in case of sight draft or accepts the usance draft undertaking to pay on maturity of the bill of exchange. The exporter’s bank receives the payment through importer's bank and is credited to exporter's account.<br>&nbsp;</p>
<p align="justify">(f) <strong>Processing of GR Form:</strong> On receiving the export proceeds, the exporter’s bank intimates the same to the RBI by recording the fact on the duplicate copy of GR. The RBI verifies the details in duplicate copy of GB with the original copy of GR received from the Customs. If the details are found to be in order then the export transaction is treated to be completed.</p>




